/// Аккумулятор, который принимает поток вещественных чисел и отвечает на
/// вопросы: сколько чисел было, каково среднее, минимум и максимум.
///
/// Поля **приватные**: снаружи структуры нельзя залезть в `sum` или `count`
/// напрямую. Единственный способ взаимодействия — публичное API (методы ниже).
/// Это и есть инкапсуляция: внутреннее представление можно поменять, не
/// трогая код, который пользуется `Stats`.
pub struct Stats {
    count: usize,
    sum: f64,
    min: f64,
    max: f64,
}

impl Stats {
    /// Создаёт пустой аккумулятор (ни одного числа не получено).
    ///
    /// Ассоциированная функция (нет `self`) — вызывается как `Stats::new()`.
    pub fn new() -> Stats {
        Stats {
            count: 0,
            sum: 0.0,
            min: f64::INFINITY,
            max: f64::NEG_INFINITY,
        }
    }

    /// Добавляет значение `v` в аккумулятор.
    ///
    /// Принимает `&mut self`: метод **изменяет** внутреннее состояние (обновляет
    /// счётчик, сумму, минимум и максимум). Без `mut` компилятор не разрешит
    /// изменять поля — это проверяется статически.
    pub fn push(&mut self, v: f64) {
        self.count += 1;
        self.sum += v;
        if v < self.min {
            self.min = v;
        }
        if v > self.max {
            self.max = v;
        }
    }

    /// Возвращает количество добавленных чисел.
    ///
    /// `&self` — только читаем состояние, ничего не меняем.
    pub fn count(&self) -> usize {
        self.count
    }

    /// Среднее арифметическое, или `None` если чисел ещё не было.
    ///
    /// `Option<f64>` честно выражает, что ответа может не быть — вместо того,
    /// чтобы возвращать магическое `0.0` или паниковать на пустом аккумуляторе.
    pub fn mean(&self) -> Option<f64> {
        if self.count == 0 {
            None
        } else {
            Some(self.sum / self.count as f64)
        }
    }

    /// Минимальное из добавленных чисел, или `None` если аккумулятор пуст.
    pub fn min(&self) -> Option<f64> {
        if self.count == 0 {
            None
        } else {
            Some(self.min)
        }
    }

    /// Максимальное из добавленных чисел, или `None` если аккумулятор пуст.
    pub fn max(&self) -> Option<f64> {
        if self.count == 0 {
            None
        } else {
            Some(self.max)
        }
    }
}
