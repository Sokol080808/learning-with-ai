// Подсказка по чтению ошибок компилятора: если Rust ругается, он почти всегда
// подсказывает, чего не хватает (& / &mut / .clone() / тип). Читай сообщение целиком —
// это не препятствие, а ревью кода прямо во время написания.

/// Точка на плоскости.
///
/// `#[derive(...)]` просит компилятор сам сгенерировать реализации:
/// - `Debug`     — печать через `{:?}` (нужно, чтобы `assert_eq!` мог показать значения);
/// - `Clone`     — явное копирование через `.clone()`;
/// - `Copy`      — побитовое копирование при присваивании (точка из двух `f64` — дёшево);
/// - `PartialEq` — сравнение `==` / `!=` по всем полям.
///
/// ПОЧЕМУ derive, а не писать руками: поведение «сравнить два `f64`-поля» очевидно и
/// механически выводится из структуры. Ручная реализация была бы шумом и местом для опечаток.
#[derive(Debug, Clone, Copy, PartialEq)]
pub struct Point {
    pub x: f64,
    pub y: f64,
}

impl Point {
    /// Ассоциированная функция-конструктор: вызывается как `Point::new(x, y)`.
    ///
    /// Это НЕ метод — у неё нет `self`. По соглашению `new` создаёт значение из частей.
    /// Возвращает владение новой точкой (вызывающий становится её хозяином).
    pub fn new(x: f64, y: f64) -> Point {
        Point { x, y }
    }

    /// Евклидово расстояние от этой точки до `other`.
    ///
    /// `&self` — метод только ЧИТАЕТ точку, не забирая владение и не меняя её.
    /// `other: &Point` — чужую точку тоже лишь одалживаем для чтения.
    /// Формула: sqrt((x1 - x2)^2 + (y1 - y2)^2). Пригодится `f64::sqrt`.
    pub fn distance_to(&self, other: &Point) -> f64 {
        let dx = self.x - other.x;
        let dy = self.y - other.y;
        (dx * dx + dy * dy).sqrt()
    }
}

/// Прямоугольник, заданный шириной и высотой.
#[derive(Debug, Clone, Copy, PartialEq)]
pub struct Rectangle {
    pub width: f64,
    pub height: f64,
}

impl Rectangle {
    /// Конструктор прямоугольника.
    pub fn new(width: f64, height: f64) -> Rectangle {
        Rectangle { width, height }
    }

    /// Площадь прямоугольника (`width * height`).
    ///
    /// `&self`, потому что считать площадь — это чтение, объект менять не нужно.
    pub fn area(&self) -> f64 {
        self.width * self.height
    }

    /// `true`, если прямоугольник — квадрат (ширина равна высоте).
    ///
    /// Возвращает `bool`, а не меняет объект — снова `&self`.
    pub fn is_square(&self) -> bool {
        self.width == self.height
    }
}

/// Кортежная структура: «обёртка» над тремя `u8` без имён полей.
///
/// Поля адресуются по индексу: `color.0`, `color.1`, `color.2`.
/// ПОЧЕМУ кортежная, а не обычная: когда смысл полей ясен из позиции (R, G, B) и
/// придумывать им имена — лишний шум, но при этом нужен ОТДЕЛЬНЫЙ ТИП (чтобы нельзя
/// было случайно перепутать цвет с любой другой тройкой чисел).
#[derive(Debug, Clone, Copy, PartialEq)]
pub struct Color(pub u8, pub u8, pub u8);

impl Color {
    /// Сумма трёх компонент как `u32`.
    ///
    /// `self` БЕЗ `&` — метод ПОТРЕБЛЯЕТ значение (забирает владение и «съедает» его).
    /// Для `Copy`-типа это не больно: вызывающий сохранит свою копию. Здесь это нужно
    /// лишь чтобы ты почувствовал разницу `self` vs `&self`: после `c.brightness()`
    /// (будь `Color` не `Copy`) переменной `c` уже нельзя было бы пользоваться.
    ///
    /// Складывай через `u32`, иначе сумма трёх `u8` может переполнить `u8`.
    pub fn brightness(self) -> u32 {
        self.0 as u32 + self.1 as u32 + self.2 as u32
    }
}
