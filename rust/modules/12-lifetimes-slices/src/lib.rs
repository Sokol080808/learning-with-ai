/// Возвращает более длинную из двух строк (по длине в БАЙТАХ).
///
/// Контракт:
/// - `longest("кот", "котик")` возвращает срез, равный `"котик"`;
/// - `longest("hello", "hi")`  возвращает `"hello"`;
/// - при равной длине вернуть ПЕРВЫЙ аргумент: `longest("ab", "cd")` == `"ab"`.
///
/// ПОЧЕМУ здесь нужна аннотация `<'a>`. Функция возвращает ссылку, но возвращаемая
/// ссылка — это ОДИН ИЗ входов, и компилятор сам не знает, который. Подпись
/// `(a: &'a str, b: &'a str) -> &'a str` обещает: результат живёт не дольше, чем
/// оба входа. Так компилятор гарантирует, что наружу не «утечёт» висячая ссылка
/// на уже освобождённые данные.
pub fn longest<'a>(a: &'a str, b: &'a str) -> &'a str {
    if b.len() > a.len() { b } else { a }
}

/// Возвращает первое слово строки — срез ДО первого пробела (пробел не включается).
/// Если пробела нет, вернуть всю строку целиком.
///
/// Контракт:
/// - `first_word("hello world")` == `"hello"`;
/// - `first_word("rust")`        == `"rust"`   (пробела нет — вся строка);
/// - `first_word("")`            == `""`;
/// - `first_word(" leading")`    == `""`        (строка начинается с пробела).
///
/// ПОЧЕМУ это безопасно. Мы НИЧЕГО не копируем: возвращаемый `&str` указывает
/// внутрь той же строки, что пришла на вход. Компилятор по правилу «элизии»
/// сам выводит: раз вход один, время жизни результата привязано к нему. Поэтому
/// явная `<'a>` тут не обязательна — но связь та же: пока живёт `s`, жив и срез.
pub fn first_word(s: &str) -> &str {
    match s.find(' ') {
        Some(i) => &s[..i],
        None => s,
    }
}

/// «Обёртка» вокруг заимствованной строки. Поле `text` НЕ владеет строкой —
/// оно лишь смотрит на чужие данные, поэтому у структуры есть время жизни `'a`.
///
/// ПОЧЕМУ структура помечена `<'a>`. Если внутри лежит ссылка, Rust обязан
/// знать, что сама обёртка не переживёт данные, на которые ссылается. Параметр
/// `'a` — это и есть обещание: «`Wrapper` живёт не дольше, чем строка `text`».
/// Попытка положить сюда ссылку на временную строку, а потом использовать
/// обёртку дальше — не скомпилируется. Это фича, а не препятствие.
pub struct Wrapper<'a> {
    /// Заимствованное окно в чужую строку. Владелец — снаружи.
    pub text: &'a str,
}

impl<'a> Wrapper<'a> {
    /// Возвращает первый СИМВОЛ строки (не байт!), или `None`, если строка пуста.
    ///
    /// Контракт:
    /// - для `Wrapper { text: "rust" }` метод даёт `Some('r')`;
    /// - для `Wrapper { text: "" }`     метод даёт `None`;
    /// - для `Wrapper { text: "ёж" }`   метод даёт `Some('ё')` (один символ Юникода,
    ///   хотя в UTF-8 он занимает 2 байта — поэтому работаем с `chars`, не с байтами).
    pub fn first_char(&self) -> Option<char> {
        self.text.chars().next()
    }
}

/// Возвращает ссылку на МАКСИМАЛЬНЫЙ элемент среза, или `None` для пустого среза.
///
/// Контракт:
/// - `max_slice(&[3, 1, 4, 1, 5, 9, 2, 6])` == `Some(&9)`;
/// - `max_slice(&[-10, -3, -7])`            == `Some(&-3)`;
/// - `max_slice(&[42])`                     == `Some(&42)`;
/// - `max_slice(&[])`                       == `None`.
///
/// ПОЧЕМУ возвращаем именно `&i64`, а не `i64`. Чтобы показать суть срезов:
/// результат — это ссылка ВНУТРЬ переданного среза, без копирования. Её время
/// жизни связано со временем жизни `v` (компилятор выводит это по элизии). Пока
/// жив `v`, ссылка валидна; как только `v` исчезнет — компилятор не даст ею
/// воспользоваться. Это и есть защита от висячих ссылок.
pub fn max_slice(v: &[i64]) -> Option<&i64> {
    v.iter().max()
}

/// Разбивает строку по пробельным символам и возвращает вектор слов.
///
/// Каждый элемент — это `&str`, смотрящий **внутрь** исходной строки `s`
/// (ноль копирований, ноль выделений новых строк). Пробельные символы
/// не включаются в результат; ведущие и хвостовые пробелы игнорируются.
///
/// Контракт:
/// - `words("hello world")` == `vec!["hello", "world"]`;
/// - `words("  spaced  ")` == `vec!["spaced"]`;
/// - `words("")` == `vec![]` (пустая строка — ноль слов);
/// - `words("one")` == `vec!["one"]` (одно слово, без пробелов);
/// - каждый элемент результата является суб-срезом `s` (проверяемо через `as_ptr()`).
///
/// Время жизни элементов вектора привязано к `s`: вектор не может пережить строку.
pub fn words(s: &str) -> Vec<&str> {
    s.split_whitespace().collect()
}

/// Возвращает самое длинное слово строки, или `None`, если слов нет.
///
/// Слова определяются так же, как в `words()` — разделитель — пробельные символы.
/// Длина измеряется в **байтах** (`len()`). При равной длине возвращается
/// **первое** встреченное слово. Возвращаемый `&str` является суб-срезом `s`.
///
/// Контракт:
/// - `longest_word("hi hello")` == `Some("hello")`;
/// - `longest_word("ab cd")` == `Some("ab")` (равная длина → первое);
/// - `longest_word("")` == `None`;
/// - `longest_word("   ")` == `None` (только пробелы — слов нет);
/// - `longest_word("rust")` == `Some("rust")`.
///
/// Явная аннотация `'a` здесь нужна, чтобы явно выразить: возвращаемая ссылка
/// живёт ровно столько, сколько входная строка `s`.
pub fn longest_word<'a>(s: &'a str) -> Option<&'a str> {
    s.split_whitespace()
        .max_by_key(|w| w.len())
        .and_then(|longest| {
            // max_by_key при равных ключах возвращает ПОСЛЕДНЕЕ — нам нужно ПЕРВОЕ.
            // Поэтому находим первое слово той же длины.
            let max_len = longest.len();
            s.split_whitespace().find(|w| w.len() == max_len)
        })
}

/// Печатает `announcement`, затем возвращает более длинную из двух строк `a` и `b`
/// (длина в БАЙТАХ, `len()`); при равной длине — ПЕРВЫЙ аргумент `a`.
///
/// Это «кульминация» главы 10.3 The Rust Book: в ОДНОЙ сигнатуре встречаются и
/// время жизни `'a`, и trait-ограничение `T: Display` — две разные системы
/// абстракций, которые компилятор проверяет независимо.
///
/// Контракт:
/// - `announce_longest("hi", "hello", "!")` возвращает `"hello"`;
/// - `announce_longest("hello", "hi", 7)`   возвращает `"hello"`;
/// - при равной длине вернуть `a`: `announce_longest("ab", "cd", x)` == `"ab"`;
/// - возвращаемый `&str` — это ОДИН ИЗ входов (суб-срез), а не копия: его
///   `.as_ptr()` совпадает с `.as_ptr()` выбранного аргумента;
/// - `announcement` может быть ЛЮБОГО типа, реализующего `Display`
///   (`&str`, `i32`, свой тип с `impl Display`).
///
/// ПОЧЕМУ обе аннотации обязательны и независимы.
/// - `'a` отвечает на вопрос «как долго жива возвращаемая ссылка»: оба входа
///   `a` и `b` помечены `'a`, результат тоже `'a` → компилятор гарантирует, что
///   наружу не утечёт висячая ссылка. `announcement` НЕ помечен `'a` (и не обязан
///   быть ссылкой вообще) — он лишь печатается и к результату отношения не имеет.
/// - `T: Display` отвечает на другой вопрос — «что мы умеем делать с
///   `announcement`»: только `{}`-форматировать. Это ограничение на ВОЗМОЖНОСТИ
///   типа, а не на его время жизни. Generic-параметр `T` и lifetime-параметр `'a`
///   живут в одном `<...>`, но решают разные задачи.
///
/// Подвох: соблазнительно пометить `announcement: &'a T` «заодно». Не нужно — это
/// без причины свяжет время жизни анонса со временем жизни результата и сузит
/// набор допустимых вызовов (например, нельзя было бы передать временный `i32`).
pub fn announce_longest<'a, T: std::fmt::Display>(a: &'a str, b: &'a str, announcement: T) -> &'a str {
    println!("Attention please: {announcement}");
    if b.len() > a.len() { b } else { a }
}
