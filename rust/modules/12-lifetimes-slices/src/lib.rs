//
// Тема модуля — СРЕЗЫ и ВРЕМЕНА ЖИЗНИ. Это сердце Rust: компилятор должен быть
// уверен, что ни одна ссылка не переживёт данные, на которые смотрит. Аннотации
// времён жизни (`'a`) — не «магия», а ПОДПИСЬ под контрактом: «возвращаемая ссылка
// живёт не дольше, чем входные». Компилятор её проверяет, а не выдумывает.
//
// Как читать сигнатуры:
//   &str          — заимствованное «окно» в строку (срез из символов в байтах UTF-8);
//   &[T]          — заимствованное окно в последовательность T (часть Vec/массива);
//   <'a> ... &'a  — «эта ссылка живёт ровно столько же, сколько помеченные 'a входы».
//
// Все тела сейчас — todo!(): код КОМПИЛИРУЕТСЯ, но падает в рантайме (тест красный).
// Твоя работа — заменить todo!() на настоящую реализацию. Сигнатуры не трогай:
// именно они задают, какие ссылки и как долго живут.

/// Возвращает более длинную из двух строк (по длине в БАЙТАХ).
///
/// Контракт:
/// - `longest("кот", "котик")` возвращает срез, равный `"котик"`;
/// - `longest("hello", "hi")`  возвращает `"hello"`;
/// - при равной длине вернуть ПЕРВЫЙ аргумент: `longest("ab", "cd")` == `"ab"`.
///
/// ПОЧЕМУ здесь нужна аннотация `<'a>`. Функция возвращает ссылку, но возвращаемая
/// ссылка — это ОДИН ИЗ входов, и компилятор сам не знает, который. Подпись
/// `(a: &'a str, b: &'a str) -> &'a str` обещает: результат живёт не дольше, чем
/// оба входа. Так компилятор гарантирует, что наружу не «утечёт» висячая ссылка
/// на уже освобождённые данные.
pub fn longest<'a>(a: &'a str, b: &'a str) -> &'a str {
    if b.len() > a.len() { b } else { a }
}

/// Возвращает первое слово строки — срез ДО первого пробела (пробел не включается).
/// Если пробела нет, вернуть всю строку целиком.
///
/// Контракт:
/// - `first_word("hello world")` == `"hello"`;
/// - `first_word("rust")`        == `"rust"`   (пробела нет — вся строка);
/// - `first_word("")`            == `""`;
/// - `first_word(" leading")`    == `""`        (строка начинается с пробела).
///
/// ПОЧЕМУ это безопасно. Мы НИЧЕГО не копируем: возвращаемый `&str` указывает
/// внутрь той же строки, что пришла на вход. Компилятор по правилу «элизии»
/// сам выводит: раз вход один, время жизни результата привязано к нему. Поэтому
/// явная `<'a>` тут не обязательна — но связь та же: пока живёт `s`, жив и срез.
pub fn first_word(s: &str) -> &str {
    match s.find(' ') {
        Some(i) => &s[..i],
        None => s,
    }
}

/// «Обёртка» вокруг заимствованной строки. Поле `text` НЕ владеет строкой —
/// оно лишь смотрит на чужие данные, поэтому у структуры есть время жизни `'a`.
///
/// ПОЧЕМУ структура помечена `<'a>`. Если внутри лежит ссылка, Rust обязан
/// знать, что сама обёртка не переживёт данные, на которые ссылается. Параметр
/// `'a` — это и есть обещание: «`Wrapper` живёт не дольше, чем строка `text`».
/// Попытка положить сюда ссылку на временную строку, а потом использовать
/// обёртку дальше — не скомпилируется. Это фича, а не препятствие.
pub struct Wrapper<'a> {
    /// Заимствованное окно в чужую строку. Владелец — снаружи.
    pub text: &'a str,
}

impl<'a> Wrapper<'a> {
    /// Возвращает первый СИМВОЛ строки (не байт!), или `None`, если строка пуста.
    ///
    /// Контракт:
    /// - для `Wrapper { text: "rust" }` метод даёт `Some('r')`;
    /// - для `Wrapper { text: "" }`     метод даёт `None`;
    /// - для `Wrapper { text: "ёж" }`   метод даёт `Some('ё')` (один символ Юникода,
    ///   хотя в UTF-8 он занимает 2 байта — поэтому работаем с `chars`, не с байтами).
    pub fn first_char(&self) -> Option<char> {
        self.text.chars().next()
    }
}

/// Возвращает ссылку на МАКСИМАЛЬНЫЙ элемент среза, или `None` для пустого среза.
///
/// Контракт:
/// - `max_slice(&[3, 1, 4, 1, 5, 9, 2, 6])` == `Some(&9)`;
/// - `max_slice(&[-10, -3, -7])`            == `Some(&-3)`;
/// - `max_slice(&[42])`                     == `Some(&42)`;
/// - `max_slice(&[])`                       == `None`.
///
/// ПОЧЕМУ возвращаем именно `&i64`, а не `i64`. Чтобы показать суть срезов:
/// результат — это ссылка ВНУТРЬ переданного среза, без копирования. Её время
/// жизни связано со временем жизни `v` (компилятор выводит это по элизии). Пока
/// жив `v`, ссылка валидна; как только `v` исчезнет — компилятор не даст ею
/// воспользоваться. Это и есть защита от висячих ссылок.
pub fn max_slice(v: &[i64]) -> Option<&i64> {
    v.iter().max()
}
