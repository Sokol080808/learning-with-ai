// Подсказка по чтению: `<T: PartialOrd>` — это «обобщённый параметр T, у которого
// есть операции сравнения (<, >, <=, >=)». Компилятор подставит конкретный тип в
// момент вызова (мономорфизация) и проверит, что ограничения выполнены. Если ты
// вызовешь функцию с типом, который не умеет сравниваться, — это ошибка КОМПИЛЯЦИИ,
// а не падение в рантайме. В этом и сила обобщений.

/// Возвращает больший из двух аргументов.
///
/// Контракт:
/// - `T` может быть любым типом, который умеет сравниваться через `<`, `>` и т.п.
///   (ограничение `T: PartialOrd`).
/// - Если `a >= b`, верни `a`, иначе `b`.
/// - Аргументы передаются ПО ЗНАЧЕНИЮ (функция забирает их во владение) и один из
///   них возвращается наружу. Это работает для любого `T`, потому что мы отдаём
///   ровно одно из полученных значений.
///
/// Примеры ожидаемого поведения:
/// - `my_max(3, 9) == 9`
/// - `my_max("apple", "banana") == "banana"` (строки сравниваются лексикографически)
pub fn my_max<T: PartialOrd>(a: T, b: T) -> T {
    if a >= b { a } else { b }
}

/// Возвращает наибольший элемент непустого среза.
///
/// Контракт:
/// - `v` — срез (`&[T]`), мы только ЧИТАЕМ его, не забирая во владение.
/// - Предполагаем, что срез НЕ пустой (тесты не передают пустой срез).
/// - Возвращаем КОПИЮ наибольшего элемента, поэтому к `T` добавлено ограничение
///   `Copy` (наряду с `PartialOrd`): нам нужно одновременно сравнивать элементы и
///   уметь скопировать выбранный наружу, не нарушая владение срезом.
///
/// Почему `Copy`, а не просто возврат ссылки? Так задание делает акцент на разнице
/// между «заимствовать срез» (`&[T]`) и «вернуть владеемое значение» (`T`).
///
/// Примеры ожидаемого поведения:
/// - `largest(&[1, 5, 2, 9, 4]) == 9`
/// - `largest(&[42]) == 42`
pub fn largest<T: PartialOrd + Copy>(v: &[T]) -> T {
    let mut best = v[0];
    for &item in &v[1..] {
        if item > best {
            best = item;
        }
    }
    best
}

/// Общий интерфейс «умею выдать краткую сводку о себе».
///
/// Трейт — это набор методов, который разные типы могут реализовать по-своему. Здесь
/// есть один обязательный метод `summarize` и один метод ПО УМОЛЧАНИЮ `preview`.
///
/// Контракт:
/// - `summarize(&self) -> String` — обязательный, каждый тип реализует сам.
/// - `preview(&self) -> String` — метод по умолчанию: возвращает строку вида
///   `"Читать дальше: <summarize>"`. Тип МОЖЕТ не переопределять его и получить эту
///   реализацию бесплатно. Реализуй тело `preview` прямо здесь, в трейте, опираясь на
///   `self.summarize()`.
pub trait Summary {
    /// Краткая сводка об объекте. Каждый реализующий тип определяет сам.
    fn summarize(&self) -> String;

    /// Метод по умолчанию: префикс `"Читать дальше: "` + результат `summarize`.
    ///
    /// Реализован прямо в трейте — типам переопределять не обязательно.
    fn preview(&self) -> String {
        format!("Читать дальше: {}", self.summarize())
    }
}

/// Статья с заголовком.
///
/// Поле `title` публичное, чтобы тесты могли создать `Article { title: ... }`.
pub struct Article {
    pub title: String,
}

impl Summary for Article {
    /// Для статьи краткая сводка — это её заголовок.
    ///
    /// Контракт: `summarize` возвращает копию `title` (новую `String`).
    /// Метод `preview` НЕ переопределяем — пусть работает реализация по умолчанию
    /// из трейта.
    fn summarize(&self) -> String {
        self.title.clone()
    }
}

