/// Точка на плоскости с координатами `x` и `y`.
///
/// `#[derive(...)]` достаёт нам базовые трейты «из коробки», без ручного `impl`:
/// - `Debug`     — печать через `{:?}` (нужно макросу `assert_eq!` для диагностики);
/// - `Clone`     — явное копирование `.clone()`;
/// - `Copy`      — `Point` состоит из двух `f64` (оба `Copy`), значит её можно копировать
///   побитово, а присваивание/передача НЕ перемещают значение;
/// - `PartialEq` — сравнение через `==` (тесты сравнивают результат сложения с ожидаемым).
///
/// Перегрузку `+` derive не умеет — её мы пишем руками ниже через трейт `std::ops::Add`.
#[derive(Debug, Clone, Copy, PartialEq)]
pub struct Point {
    pub x: f64,
    pub y: f64,
}

/// Перегрузка оператора `+` для `Point`.
///
/// Контракт:
/// - `Point { x: 1.0, y: 2.0 } + Point { x: 3.0, y: 4.0 } == Point { x: 4.0, y: 6.0 }` —
///   складываем покоординатно.
/// - `type Output = Point` — это АССОЦИИРОВАННЫЙ ТИП трейта `Add`: он отвечает на
///   вопрос «а что вернёт `+`?». Для `Point + Point` результат — снова `Point`.
///   В отличие от обычного параметра типа, `Output` не выбирает вызывающий: он
///   ОДНОЗНАЧНО определён реализацией (на пару «тип слева, тип справа» — ровно один `Output`).
///
/// ПОЧЕМУ через трейт, а не «просто функцию `add`»: реализовав `std::ops::Add`, ты
/// учишь сам оператор `+` работать с твоим типом. Компилятор переписывает `a + b`
/// в `Add::add(a, b)`. Это даёт привычный синтаксис вместо `a.add(b)` и позволяет
/// обобщённому коду (`fn sum<T: Add<Output = T>>(...)`) принимать твой `Point` наравне
/// со встроенными числами.
impl std::ops::Add for Point {
    /// Тип результата сложения. Связан с реализацией, а не выбирается вызывающим.
    type Output = Point;

    /// Складывает две точки покоординатно и возвращает новую `Point`.
    fn add(self, other: Point) -> Point {
        Point {
            x: self.x + other.x,
            y: self.y + other.y,
        }
    }
}
