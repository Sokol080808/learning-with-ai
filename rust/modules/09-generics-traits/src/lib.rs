/// Трекер минимального и максимального значения для потока данных произвольного типа.
///
/// Хранит внутреннее состояние через серию вызовов `push`. После каждого `push`
/// `min()` и `max()` возвращают актуальные экстремумы; до первого `push` оба метода
/// возвращают `None`.
///
/// Ограничения на `T`:
/// - `PartialOrd` — нужно для сравнений (определить, кто меньше/больше).
/// - `Copy`       — нужно для хранения копии значения и возврата его по значению.
///
/// Работает одинаково для `i32`, `f64`, `char` и любого другого `Copy + PartialOrd` типа.
pub struct MinMaxTracker<T> {
    min: Option<T>,
    max: Option<T>,
    count: usize,
}

impl<T: PartialOrd + Copy> MinMaxTracker<T> {
    /// Создаёт новый пустой трекер (ещё ни одного значения не получено).
    ///
    /// Контракт: сразу после `new()` — `min()` и `max()` возвращают `None`,
    /// `count()` возвращает `0`.
    pub fn new() -> Self {
        MinMaxTracker {
            min: None,
            max: None,
            count: 0,
        }
    }

    /// Подаёт значение `v` в трекер.
    ///
    /// Контракт: после вызова `count` увеличивается на 1; `min` и `max` обновляются,
    /// если `v` оказался меньше текущего минимума или больше текущего максимума.
    pub fn push(&mut self, v: T) {
        self.count += 1;
        self.min = Some(match self.min {
            None => v,
            Some(cur) => if v < cur { v } else { cur },
        });
        self.max = Some(match self.max {
            None => v,
            Some(cur) => if v > cur { v } else { cur },
        });
    }

    /// Возвращает текущий минимум, или `None` если трекер пуст.
    pub fn min(&self) -> Option<T> {
        self.min
    }

    /// Возвращает текущий максимум, или `None` если трекер пуст.
    pub fn max(&self) -> Option<T> {
        self.max
    }

    /// Возвращает количество значений, поданных через `push`.
    pub fn count(&self) -> usize {
        self.count
    }
}

