/// Сумма ПЕРВЫХ `n` элементов среза `v`.
///
/// Контракт: если `n >= v.len()`, суммируются все элементы (выход за границы недопустим).
/// Например, `sum_first_n(&[10, 20, 30, 40], 2) == 30` (берём `10` и `20`).
///
/// `v: &[i64]` — мы лишь ОДАЛЖИВАЕМ срез для чтения, не забирая владение и не меняя его.
/// Поэтому после вызова вызывающий по-прежнему владеет своим вектором.
///
/// ⚠️ Здесь притаился баг вида «off-by-one» (ошибка на единицу) — самый частый баг в циклах.
pub fn sum_first_n(v: &[i64], n: usize) -> i64 {
    let limit = if n < v.len() { n } else { v.len() };
    let mut total = 0;
    // БАГ: цикл начинается с 1, а не с 0 — первый элемент теряется.
    for i in 0..limit {
        total += v[i];
    }
    total
}

/// Максимальный элемент непустого среза `v`.
///
/// Контракт: тесты НЕ передают пустой срез. Должно работать и для отрицательных чисел:
/// `max_in(&[-5, -2, -9]) == -2`.
///
/// ⚠️ Баг — в НАЧАЛЬНОМ значении «текущего максимума». Подумай: с чего честно начать
/// поиск максимума? С нуля — нечестно, ведь данные могут быть все отрицательные.
pub fn max_in(v: &[i64]) -> i64 {
    // БАГ: стартуем с 0. Для среза из одних отрицательных чисел вернётся 0 — а такого
    // элемента в данных нет. Правильный старт — первый элемент среза, v[0].
    let mut best = v[0];
    for &x in v {
        if x > best {
            best = x;
        }
    }
    best
}

/// Среднее арифметическое непустого среза `v`.
///
/// Контракт: тесты НЕ передают пустой срез. `average(&[2, 4]) == 3.0`,
/// `average(&[1, 2, 3, 4]) == 2.5`. Возвращаем `f64`, потому что среднее целых
/// чисел дробное.
///
/// ⚠️ Два возможных бага в одном месте: (1) на что делить и (2) целочисленное деление
/// вместо вещественного. Сумму и количество нужно привести к `f64` ДО деления, иначе
/// `7 / 2` посчитается как `3`, а не `3.5`.
pub fn average(v: &[i64]) -> f64 {
    let mut sum = 0i64;
    for &x in v {
        sum += x;
    }
    // БАГ: делим на (len - 1) вместо len, да ещё и целочисленно (приводим к f64 уже
    // ПОСЛЕ деления, когда дробная часть потеряна).
    sum as f64 / v.len() as f64
}

/// `true`, если в срезе `v` есть хотя бы два РАЗНЫХ по позиции элемента с одинаковым
/// значением (то есть значение повторяется).
///
/// Контракт: `has_duplicate(&[1, 2, 3]) == false`, `has_duplicate(&[1, 2, 1]) == true`,
/// `has_duplicate(&[]) == false`.
///
/// ⚠️ Баг — элемент сравнивается САМ С СОБОЙ. Любой `v[i] == v[i]` — это всегда `true`,
/// поэтому функция «находит дубликат» даже там, где все элементы уникальны. Внутренний
/// цикл должен начинаться ПОСЛЕ текущего индекса, а не с него.
pub fn has_duplicate(v: &[i64]) -> bool {
    let n = v.len();
    for i in 0..n {
        // БАГ: j стартует с i, значит первая же проверка — v[i] == v[i] == true.
        // Нужно j in (i + 1)..n, чтобы сравнивать с ПОСЛЕДУЮЩИМИ элементами.
        for j in (i + 1)..n {
            if v[i] == v[j] {
                return true;
            }
        }
    }
    false
}

/// Повторяет строку `s` ровно `times` раз и склеивает результат.
///
/// Контракт: `repeat_str("ab", 3) == "ababab"`, `repeat_str("x", 0) == ""`,
/// `repeat_str("hi", 1) == "hi"`.
///
/// Возвращает `String` (владеющую строку): мы СОЗДАЁМ новые данные, поэтому возвращаем
/// владение ими, а не ссылку на временный буфер.
///
/// ⚠️ Снова off-by-one: повторов на один меньше, чем просили.
pub fn repeat_str(s: &str, times: usize) -> String {
    let mut result = String::new();
    if times == 0 {
        return result;
    }
    // БАГ: повторяем (times - 1) раз вместо times. Для times == 1 вернётся пустая строка.
    for _ in 0..times {
        result.push_str(s);
    }
    result
}
