/// Складывает два целых числа и возвращает их сумму.
///
/// # Контракт
/// - `add(2, 3)` == `5`
/// - `add(-4, 4)` == `0`
/// - `add(0, 0)` == `0`
///
/// Тип `i64` — это знаковое 64-битное целое (умещается примерно
/// ±9.2 * 10^18). В этом модуле о переполнении думать не нужно: тесты
/// используют маленькие числа.
pub fn add(a: i64, b: i64) -> i64 {
    // Подсказка: тело — буквально одно выражение. В Rust последнее выражение
    // БЕЗ точки с запятой становится возвращаемым значением функции.
    a + b
}

/// Сколько секунд в заданном числе часов.
///
/// В одном часе 3600 секунд (60 минут * 60 секунд), поэтому ответ —
/// это `3600 * hours`.
///
/// # Контракт
/// - `seconds_in(1)`  == `3600`
/// - `seconds_in(2)`  == `7200`
/// - `seconds_in(0)`  == `0`
/// - `seconds_in(24)` == `86_400`
pub fn seconds_in(hours: i64) -> i64 {
    3600 * hours
}
