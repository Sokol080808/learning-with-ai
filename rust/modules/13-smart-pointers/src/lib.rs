/// Узел дерева с разделяемым изменяемым доступом.
///
/// Каждый узел хранит целое значение `value` и список дочерних узлов.
/// Дети представлены как `Rc<RefCell<TreeNode>>`:
/// - `Rc` позволяет нескольким частям кода держать ссылку на один и тот же узел
///   без единственного владельца (важно при построении деревьев «сверху вниз»);
/// - `RefCell` позволяет добавлять детей через `&self`, не требуя `&mut self` у родителя
///   (рантаймовая проверка правил заимствования вместо компиляционной).
///
/// Типовые операции реализованы как свободные функции, а не методы — чтобы
/// подчеркнуть, что снаружи мы работаем именно через `Rc<RefCell<TreeNode>>`,
/// а не через «голый» `TreeNode`.
pub struct TreeNode {
    /// Значение узла.
    pub value: i64,
    /// Дочерние узлы; каждый — разделяемый изменяемый указатель.
    pub children: Vec<Rc<RefCell<TreeNode>>>,
}

impl TreeNode {
    /// Создать новый узел с заданным значением и без детей.
    ///
    /// Возвращает `Rc<RefCell<TreeNode>>` — стандартная «обёртка» для разделяемого
    /// изменяемого дерева; нет смысла создавать «голый» `TreeNode` вне неё.
    ///
    /// Контракт: `TreeNode::new(v).borrow().value == v`,
    /// `TreeNode::new(v).borrow().children.is_empty() == true`.
    pub fn new(value: i64) -> Rc<RefCell<Self>> {
        Rc::new(RefCell::new(TreeNode {
            value,
            children: Vec::new(),
        }))
    }

    /// Добавить `child` как дочерний узел к `parent`.
    ///
    /// Контракт: после вызова `parent.borrow().children` содержит `child`.
    ///
    /// Важно: метод берёт `borrow_mut()` на родителя только на время добавления,
    /// а не удерживает его — это позволяет вызывать `add_child` несколько раз подряд
    /// без паники RefCell.
    pub fn add_child(parent: &Rc<RefCell<Self>>, child: Rc<RefCell<Self>>) {
        parent.borrow_mut().children.push(child);
    }

    /// Рекурсивная сумма всех значений в поддереве с корнем `node`.
    ///
    /// Контракт:
    /// - для листа (без детей): вернуть `node.borrow().value`;
    /// - для узла с детьми: `value` + сумма всех поддеревьев.
    ///
    /// Ключевая тонкость: заём (`borrow()`) нельзя удерживать во время рекурсивного
    /// вызова на детях — иначе при определённых топологиях RefCell запаникует. Поэтому
    /// сначала извлекаем `value` и клонируем `children`, отпускаем заём, и только потом
    /// рекурсируем.
    pub fn sum_tree(node: &Rc<RefCell<Self>>) -> i64 {
        // Берём заём, извлекаем нужное, сразу отпускаем.
        let (value, children) = {
            let borrowed = node.borrow();
            (borrowed.value, borrowed.children.clone())
        };
        // Рекурсия происходит уже без живого займа на `node`.
        children
            .iter()
            .fold(value, |acc, child| acc + TreeNode::sum_tree(child))
    }

    /// Максимальная глубина поддерева с корнем `node`.
    ///
    /// Контракт:
    /// - для листа: `1` (сам узел — уровень 1);
    /// - для узла с детьми: `1 + max(depth(child) for child in children)`.
    ///
    /// Та же тонкость, что и в `sum_tree`: отпускаем заём до рекурсии.
    pub fn depth(node: &Rc<RefCell<Self>>) -> usize {
        let children = {
            let borrowed = node.borrow();
            borrowed.children.clone()
        };
        if children.is_empty() {
            1
        } else {
            1 + children
                .iter()
                .map(|child| TreeNode::depth(child))
                .max()
                .unwrap_or(0)
        }
    }
}
