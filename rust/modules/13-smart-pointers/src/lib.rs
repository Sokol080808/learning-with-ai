/// Кэшированное значение, которое «подсматривает» за внешним источником через
/// **слабую** ссылку.
///
/// Это маленькая модель наблюдателя, который не должен удерживать источник в
/// живых: он лишь читает его, *пока тот существует*. Два слоя умных указателей
/// работают здесь в паре:
///
/// - `source: Weak<RefCell<i64>>` — слабая ссылка на источник. ПОЧЕМУ именно
///   `Weak`, а не `Rc`: сильная ссылка не дала бы источнику умереть, пока жив
///   кэш (как `parent`-ссылка в дереве создаёт цикл). Слабая ссылка **не**
///   увеличивает счётчик владельцев — источник освобождается, как только исчезает
///   последний сильный `Rc`, независимо от кэша.
/// - `cache: Cell<i64>` — последнее прочитанное значение. ПОЧЕМУ `Cell`, а не
///   обычное поле: `refresh` и `cached` берут `&self` (а не `&mut self`), но
///   `refresh` должен записать новое значение — это внутренняя изменяемость.
///   Содержимое `i64` реализует `Copy`, поэтому хватает простого `Cell`
///   (`get`/`set`, без рантайм-проверки заёма), а не `RefCell`.
pub struct CachedValue {
    /// Последнее значение, прочитанное из источника (кэш).
    cache: Cell<i64>,
    /// Слабая ссылка на источник; может «протухнуть», если источник дропнут.
    source: Weak<RefCell<i64>>,
}

impl CachedValue {
    /// Создать кэш, наблюдающий за `source`.
    ///
    /// Принимает `&Rc<RefCell<i64>>` (а не забирает владение) и понижает его до
    /// слабой ссылки через `Rc::downgrade`. Так создание кэша **не** продлевает
    /// жизнь источника. Начальное значение кэша — `0` (источник ещё не читали).
    ///
    /// Контракт: сразу после `new` поле `cached()` равно `0`, пока не вызван
    /// `refresh`.
    pub fn new(source: &Rc<RefCell<i64>>) -> Self {
        CachedValue {
            cache: Cell::new(0),
            source: Rc::downgrade(source),
        }
    }

    /// Обновить кэш из источника, если тот ещё жив.
    ///
    /// Контракт:
    /// - если источник жив (`upgrade()` даёт `Some(rc)`): прочитать его значение
    ///   через `rc.borrow()`, записать в `cache` и вернуть `Some(значение)`;
    /// - если источник уже дропнут (`upgrade()` даёт `None`): **не** трогать
    ///   кэш и вернуть `None`.
    ///
    /// Ключевая идея: `Weak::upgrade()` — единственный способ добраться до данных
    /// через слабую ссылку, и он честно сообщает `None`, когда данных уже нет.
    /// Метод берёт `&self`: запись в `cache` возможна благодаря `Cell`.
    pub fn refresh(&self) -> Option<i64> {
        let rc = self.source.upgrade()?;
        let value = *rc.borrow();
        self.cache.set(value);
        Some(value)
    }

    /// Вернуть последнее закэшированное значение.
    ///
    /// Контракт: возвращает то, что лежит в `cache` сейчас — `0` до первого
    /// успешного `refresh`, иначе значение из последнего успешного `refresh`.
    /// Это значение **не зависит** от того, жив ли ещё источник: даже после того
    /// как источник дропнут, последний кэш остаётся доступным.
    pub fn cached(&self) -> i64 {
        self.cache.get()
    }
}
