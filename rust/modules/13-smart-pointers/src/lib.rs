use std::cell::RefCell;
use std::rc::Rc;

/// Cons-список (как в Lisp): либо «голова + хвост», либо конец.
///
/// Это рекурсивный тип: вариант `Cons` хранит хвост типа `List` внутри `Box`.
/// ПОЧЕМУ именно `Box`: без него `List` содержал бы `List` напрямую, и компилятор
/// не смог бы вычислить размер типа (он был бы «бесконечным»). `Box<List>` — это
/// указатель фиксированного размера на кучу, поэтому размер `List` снова конечен.
///
/// - `Cons(i64, Box<List>)` — значение `i64` и хвост (остаток списка) в куче;
/// - `Nil`                  — пустой хвост, конец списка.
#[derive(Debug, PartialEq)]
pub enum List {
    Cons(i64, Box<List>),
    Nil,
}

impl List {
    /// Длина списка — сколько в нём элементов (узлов `Cons`).
    ///
    /// Контракт:
    /// - для `Nil` вернуть `0`;
    /// - для `Cons(_, tail)` вернуть `1 + длина хвоста` (рекурсивно).
    ///
    /// Реализуй через `match self { ... }`, разбирая текущий узел на голову и хвост.
    pub fn len(&self) -> usize {
        match self {
            List::Nil => 0,
            List::Cons(_, tail) => 1 + tail.len(),
        }
    }

    /// Сумма всех значений в списке.
    ///
    /// Контракт:
    /// - для `Nil` вернуть `0`;
    /// - для `Cons(v, tail)` вернуть `v + сумма хвоста` (рекурсивно).
    pub fn sum(&self) -> i64 {
        match self {
            List::Nil => 0,
            List::Cons(v, tail) => v + tail.sum(),
        }
    }
}

/// Собрать cons-список из среза значений, сохраняя порядок.
///
/// Контракт:
/// - `build_list(&[])`        == `List::Nil`;
/// - `build_list(&[1, 2, 3])` == `Cons(1, Cons(2, Cons(3, Nil)))`,
///   то есть первый элемент среза становится головой всего списка.
///
/// `items: &[i64]` — срез по ссылке: мы только читаем чужие данные, не владея ими.
/// Подсказка: список строится «с хвоста к голове», поэтому удобно идти с конца среза.
pub fn build_list(items: &[i64]) -> List {
    let mut acc = List::Nil;
    for &x in items.iter().rev() {
        acc = List::Cons(x, Box::new(acc));
    }
    acc
}

/// Счётчик, который умеет меняться через общую ссылку `&self`.
///
/// Поле `count` — это `RefCell<i64>`. ПОЧЕМУ так: обычно, чтобы изменить поле, нужен
/// `&mut self`. Но `RefCell` даёт *внутреннюю изменяемость* — он позволяет менять
/// содержимое даже через `&self`, перенося проверку правил заимствования с момента
/// компиляции в рантайм. Снаружи объект выглядит «неизменяемым», а внутри — меняется.
pub struct Counter {
    pub count: RefCell<i64>,
}

impl Counter {
    /// Создать счётчик с начальным значением `0`.
    ///
    /// Положи `0` внутрь `RefCell`: `RefCell::new(0)`.
    pub fn new() -> Self {
        Counter {
            count: RefCell::new(0),
        }
    }

    /// Увеличить счётчик на 1 — обрати внимание: метод берёт `&self`, а не `&mut self`.
    ///
    /// Контракт: после вызова значение внутри `count` становится на 1 больше.
    /// Подсказка: `self.count.borrow_mut()` даёт изменяемый доступ к содержимому в
    /// рантайме; разыменуй его (`*`) и прибавь 1.
    pub fn increment(&self) {
        *self.count.borrow_mut() += 1;
    }

    /// Текущее значение счётчика.
    ///
    /// Подсказка: `self.count.borrow()` даёт доступ для чтения; разыменуй (`*`) и верни.
    pub fn get(&self) -> i64 {
        *self.count.borrow()
    }
}

impl Default for Counter {
    fn default() -> Self {
        Self::new()
    }
}

/// Продемонстрировать рост счётчика сильных ссылок у `Rc`.
///
/// Контракт (что должно произойти ВНУТРИ функции):
/// 1. создать `Rc::new(...)` с любым значением `i64` — это одна сильная ссылка;
/// 2. сделать ещё хотя бы один `Rc::clone(...)` от него — счётчик станет 2;
/// 3. вернуть `Rc::strong_count(...)` для этого `Rc`.
///
/// ПОЧЕМУ `Rc::clone`, а не `.clone()`: `Rc::clone` лишь увеличивает счётчик ссылок и
/// НЕ копирует данные — это дёшево. Запись `Rc::clone(&a)` подчёркивает, что мы делаем
/// именно разделяемое владение, а не глубокую копию.
///
/// Тест проверяет, что возвращённое число равно количеству живых клонов (>= 2).
pub fn rc_clone_count() -> usize {
    let a = Rc::new(42i64);
    let _b = Rc::clone(&a);
    Rc::strong_count(&a)
}
