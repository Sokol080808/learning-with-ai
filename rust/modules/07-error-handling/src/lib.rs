/// Разобрать строку в целое число `i64`.
///
/// Контракт:
/// - если `s` — корректная запись целого числа (возможно со знаком, напр. "42",
///   "-7", "+3"), вернуть `Ok(число)`;
/// - иначе вернуть `Err(сообщение)`, где сообщение содержит исходную строку,
///   например: `not a number: 'abc'`.
///
/// ПОЧЕМУ возвращаем `Result`, а не паникуем: разбор строки — типичная
/// «восстановимая» ошибка. Вызывающий код сам решит, что делать с плохим вводом,
/// а не упадёт целиком.
pub fn parse_int(s: &str) -> Result<i64, String> {
    s.parse::<i64>().map_err(|_| format!("not a number: '{s}'"))
}

/// Целочисленное деление `a / b`.
///
/// Контракт:
/// - при `b != 0` вернуть `Ok(a / b)`;
/// - при `b == 0` вернуть `Err("division by zero".to_string())` (деление на ноль —
///   восстановимая ситуация: мы НЕ хотим, чтобы программа падала).
pub fn divide(a: i64, b: i64) -> Result<i64, String> {
    if b == 0 {
        Err("division by zero".to_string())
    } else {
        Ok(a / b)
    }
}

/// Взять элемент среза по индексу.
///
/// Контракт:
/// - если `i` в границах (`i < v.len()`), вернуть `Ok(v[i])`;
/// - иначе вернуть `Err(сообщение)` с индексом и длиной, например:
///   `index 5 out of bounds for length 3`.
///
/// Принимаем `&[i64]` (заимствование среза, без владения): нам нужно только
/// прочитать элемент. Возвращаем `i64` по значению — `i64` дёшево копируется (`Copy`).
pub fn element_at(v: &[i64], i: usize) -> Result<i64, String> {
    v.get(i)
        .copied()
        .ok_or_else(|| format!("index {i} out of bounds for length {}", v.len()))
}

/// Просуммировать числа, записанные строками.
///
/// Контракт:
/// - разобрать КАЖДУЮ строку из `parts` в число и вернуть `Ok(сумма)`;
/// - если ХОТЯ БЫ ОДНА часть не число — немедленно вернуть `Err` от первого
///   неудачного разбора (то самое сообщение, что отдаёт `parse_int`);
/// - для пустого среза сумма равна `Ok(0)`.
///
/// Здесь по задумке используется оператор `?`: он берёт `Result`, и если внутри
/// `Err` — досрочно возвращает его наружу как результат всей функции, а если `Ok` —
/// разворачивает значение и идёт дальше. Это и есть «ранний возврат ошибки» без
/// лесенки из `match`.
pub fn sum_parsed(parts: &[&str]) -> Result<i64, String> {
    let mut total = 0i64;
    for part in parts {
        total += parse_int(part)?;
    }
    Ok(total)
}

