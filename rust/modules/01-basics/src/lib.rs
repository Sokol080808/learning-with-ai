/// Переводит температуру из градусов Цельсия в градусы Фаренгейта.
///
/// Формула: `F = C * 9/5 + 32`.
///
/// Контракт:
/// - `to_fahrenheit(0.0)` == `32.0`
/// - `to_fahrenheit(100.0)` == `212.0`
/// - `to_fahrenheit(-40.0)` == `-40.0`  (точка, где шкалы совпадают)
///
/// Подвох: тип `9/5` важен. Если написать `9 / 5` целыми числами,
/// получится `1` (целочисленное деление), а не `1.8`. Считай во `f64`.
pub fn to_fahrenheit(c: f64) -> f64 {
    c * 9.0 / 5.0 + 32.0
}

/// Возвращает `true`, если число чётное, и `false`, если нечётное.
///
/// Чётное — то, что делится на 2 без остатка.
///
/// Контракт:
/// - `is_even(0)` == `true`
/// - `is_even(4)` == `true`
/// - `is_even(7)` == `false`
/// - `is_even(-2)` == `true`   (отрицательные тоже бывают чётными)
/// - `is_even(-3)` == `false`
///
/// Подсказка: оператор остатка от деления — `%`. Сравни `n % 2` с нулём.
pub fn is_even(n: i64) -> bool {
    n % 2 == 0
}

/// Возвращает наименьшее из трёх чисел.
///
/// Контракт:
/// - `min_of_three(3, 1, 2)` == `1`
/// - `min_of_three(5, 5, 5)` == `5`   (все равны — это и есть минимум)
/// - `min_of_three(-1, -7, 4)` == `-7`
///
/// Подсказка: можно через `if`/`else`, а можно вспомнить, что у `i64`
/// есть метод `.min(...)`. Оба пути годятся.
pub fn min_of_three(a: i64, b: i64, c: i64) -> i64 {
    a.min(b).min(c)
}

/// Возвращает абсолютную разницу двух чисел — расстояние между ними
/// на числовой прямой. Результат всегда неотрицательный.
///
/// Контракт:
/// - `abs_diff(10, 3)` == `7`
/// - `abs_diff(3, 10)` == `7`    (порядок аргументов не важен)
/// - `abs_diff(-5, 5)` == `10`
/// - `abs_diff(4, 4)` == `0`
///
/// Подсказка: вычти одно из другого, а у разницы возьми модуль.
/// У `i64` есть метод `.abs()`. Подумай, что произойдёт без него,
/// если первый аргумент меньше второго.
pub fn abs_diff(a: i64, b: i64) -> i64 {
    (a - b).abs()
}

/// Возвращает FizzBuzz-последовательность для чисел от 1 до `n` включительно.
///
/// Правила для каждого числа `i` в диапазоне `1..=n`:
/// - делится на 3 и на 5 → `"FizzBuzz"`
/// - делится только на 3  → `"Fizz"`
/// - делится только на 5  → `"Buzz"`
/// - иначе                → строковое представление числа (например, `"7"`)
///
/// При `n == 0` возвращает пустой вектор.
///
/// Контракт:
/// - `fizzbuzz_vec(0)` == `[]`
/// - `fizzbuzz_vec(1)` == `["1"]`
/// - `fizzbuzz_vec(5)` == `["1","2","Fizz","4","Buzz"]`
/// - `fizzbuzz_vec(15)[14]` == `"FizzBuzz"`
/// - `fizzbuzz_vec(n).len()` == `n as usize` для любого `n`
pub fn fizzbuzz_vec(n: u64) -> Vec<String> {
    let mut result = Vec::new();
    for i in 1..=n {
        let s = if i % 15 == 0 {
            "FizzBuzz".to_string()
        } else if i % 3 == 0 {
            "Fizz".to_string()
        } else if i % 5 == 0 {
            "Buzz".to_string()
        } else {
            i.to_string()
        };
        result.push(s);
    }
    result
}
