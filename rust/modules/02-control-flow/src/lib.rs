/// FizzBuzz для чисел от 1 до `n` включительно.
///
/// Возвращает вектор строк длины `n`. Для каждого числа `i` (1..=n):
/// - кратно и 3, и 5  -> `"FizzBuzz"`
/// - кратно 3         -> `"Fizz"`
/// - кратно 5         -> `"Buzz"`
/// - иначе            -> само число как строка, напр. `"7"`
///
/// При `n == 0` возвращается пустой вектор.
///
/// Примеры:
/// - `fizzbuzz(3)` -> `["1", "2", "Fizz"]`
/// - `fizzbuzz(5)` -> `["1", "2", "Fizz", "4", "Buzz"]`
///
/// Подумай: порядок проверок важен. Сначала самый «узкий» случай (и 3, и 5),
/// иначе FizzBuzz никогда не сработает. `match (i % 3, i % 5)` читается красиво.
pub fn fizzbuzz(n: u32) -> Vec<String> {
    let mut out: Vec<String> = Vec::new();
    for i in 1..=n {
        let s = match (i % 3, i % 5) {
            (0, 0) => "FizzBuzz".to_string(),
            (0, _) => "Fizz".to_string(),
            (_, 0) => "Buzz".to_string(),
            _ => i.to_string(),
        };
        out.push(s);
    }
    out
}

/// Факториал: `n! = 1 * 2 * ... * n`.
///
/// По определению `factorial(0) == 1`.
/// Тесты не передают значения, при которых результат не влезает в `u64`.
///
/// Примеры:
/// - `factorial(0)` -> `1`
/// - `factorial(5)` -> `120`
///
/// Подумай: накапливай произведение в переменной `let mut acc = 1u64;`,
/// проходя `for k in 1..=n`. Тип `n` — `u64`, диапазон `1..=n` это умеет.
pub fn factorial(n: u64) -> u64 {
    let mut acc = 1u64;
    for k in 1..=n {
        acc *= k;
    }
    acc
}

/// `n`-е число Фибоначчи (нумерация с нуля).
///
/// Ряд: 0, 1, 1, 2, 3, 5, 8, 13, ...
/// То есть `fib(0) == 0`, `fib(1) == 1`, `fib(n) == fib(n-1) + fib(n-2)`.
///
/// Примеры:
/// - `fib(0)`  -> `0`
/// - `fib(1)`  -> `1`
/// - `fib(10)` -> `55`
///
/// Подумай: НЕ пиши рекурсию — она экспоненциально медленна. Держи два последних
/// значения (`a`, `b`) и крути цикл, сдвигая их. Это итеративный, линейный способ.
pub fn fib(n: u32) -> u64 {
    let (mut a, mut b) = (0u64, 1u64);
    for _ in 0..n {
        let next = a + b;
        a = b;
        b = next;
    }
    a
}

/// Наибольший общий делитель по алгоритму Евклида.
///
/// `gcd(a, b)` — наибольшее число, на которое делятся и `a`, и `b`.
/// Соглашения: `gcd(a, 0) == a`, `gcd(0, b) == b`, `gcd(0, 0) == 0`.
///
/// Примеры:
/// - `gcd(12, 18)` -> `6`
/// - `gcd(17, 5)`  -> `1`
/// - `gcd(0, 9)`   -> `9`
///
/// Подумай: классический Евклид — `while b != 0 { (a, b) = (b, a % b); }`,
/// в конце ответ лежит в `a`. Rust умеет присваивать кортежем сразу две переменные.
pub fn gcd(a: u64, b: u64) -> u64 {
    let (mut a, mut b) = (a, b);
    while b != 0 {
        (a, b) = (b, a % b);
    }
    a
}

/// Классификация числа по знаку через `match`.
///
/// Возвращает строковый литерал со статической временем жизни (`&'static str`):
/// - `n < 0` -> `"negative"`
/// - `n == 0` -> `"zero"`
/// - `n > 0` -> `"positive"`
///
/// Примеры:
/// - `classify(-3)` -> `"negative"`
/// - `classify(0)`  -> `"zero"`
/// - `classify(7)`  -> `"positive"`
///
/// Подумай: почему возвращаемый тип — `&'static str`, а не `String`? Потому что
/// строковые литералы вшиты в бинарник и живут всю программу — владеть и
/// аллоцировать ничего не нужно. `match` по сравнению `n.cmp(&0)` тоже здесь уместен.
pub fn classify(n: i64) -> &'static str {
    use std::cmp::Ordering;
    match n.cmp(&0) {
        Ordering::Less => "negative",
        Ordering::Equal => "zero",
        Ordering::Greater => "positive",
    }
}

/// Кодирование серий (run-length encoding) строки символов.
///
/// Проходит по символам строки `s`, группирует подряд идущие одинаковые символы
/// в «серии» и возвращает вектор пар `(символ, длина_серии)` в порядке следования.
///
/// Контракт:
/// - Для пустой строки возвращается пустой вектор.
/// - Каждая пара `(c, n)` означает: символ `c` встречается подряд `n` раз (n >= 1).
/// - Если «развернуть» результат обратно (каждую пару -> n раз символ c),
///   получится исходная строка `s`.
///
/// Примеры:
/// - `run_length_encode("aabccc")` -> `[('a', 2), ('b', 1), ('c', 3)]`
/// - `run_length_encode("")`       -> `[]`
/// - `run_length_encode("abc")`    -> `[('a', 1), ('b', 1), ('c', 1)]`
/// - `run_length_encode("aaaa")`   -> `[('a', 4)]`
///
/// Реализация демонстрирует:
/// - `for c in s.chars()` — итерация по символам (char — тип Copy);
/// - `match` по `Option<(char, usize)>` с обеими ветками (`Some` / `None`);
/// - сброс текущей серии в вектор при смене символа.
pub fn run_length_encode(s: &str) -> Vec<(char, usize)> {
    let mut result: Vec<(char, usize)> = Vec::new();
    let mut current: Option<(char, usize)> = None;

    for c in s.chars() {
        current = match current {
            // Продолжаем текущую серию — символ совпадает
            Some((prev, count)) if prev == c => Some((prev, count + 1)),
            // Символ сменился — сбрасываем предыдущую серию и начинаем новую
            Some((prev, count)) => {
                result.push((prev, count));
                Some((c, 1))
            }
            // Начало строки — первый символ
            None => Some((c, 1)),
        };
    }

    // Сбрасываем последнюю незавершённую серию, если строка непуста
    if let Some((c, count)) = current {
        result.push((c, count));
    }

    result
}
