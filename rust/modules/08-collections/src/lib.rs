use std::collections::HashMap;

/// Подсчитать, сколько раз встречается каждый символ строки `s`.
///
/// Возвращает отображение `символ -> количество вхождений`. Символы, которых в строке
/// нет, в карте не появляются (нулевых счётчиков не бывает).
///
/// Считаем именно `char` (скалярные значения Unicode), а не байты: для `"é"` это один
/// символ, даже если в UTF-8 он занимает два байта. Поэтому итерируемся по `s.chars()`.
///
/// Пример: для `"aab"` → `{'a': 2, 'b': 1}`.
///
/// Заметь: мы лишь читаем `s` (он `&str`, чужой), а ключи-`char` копируются в карту
/// (это `Copy`-тип), поэтому со временем жизни здесь возиться не приходится.
pub fn char_freq(s: &str) -> HashMap<char, usize> {
    let mut map = HashMap::new();
    for c in s.chars() {
        *map.entry(c).or_insert(0) += 1;
    }
    map
}

/// Вернуть уникальные элементы `v`, отсортированные по возрастанию.
///
/// На вход берём вектор ПО ЗНАЧЕНИЮ (`Vec<i64>`) — то есть забираем владение и вольны
/// его менять/сортировать как угодно. На выход отдаём новый `Vec<i64>`, где каждое
/// значение встречается ровно один раз, а порядок — строго по возрастанию.
///
/// Пример: `vec![3, 1, 2, 3, 1]` → `vec![1, 2, 3]`. Для пустого входа — пустой выход.
///
/// Подсказка к смыслу сигнатуры: раз мы владеем `v`, нам не нужны ни `&`, ни клонирование —
/// можно отсортировать вектор на месте, а потом убрать соседние дубликаты.
pub fn dedup_sorted(v: Vec<i64>) -> Vec<i64> {
    let mut v = v;
    v.sort();
    v.dedup();
    v
}

/// Посчитать частоту слов в тексте `text`, разбивая его по пробельным символам.
///
/// «Слово» — это непустой кусок между пробелами (используй `split_whitespace`, он
/// аккуратно съедает любые подряд идущие пробелы/табы/переводы строк и не порождает
/// пустых слов). Регистр НЕ нормализуем: `"Cat"` и `"cat"` — разные слова.
///
/// Ключ карты — `String` (мы ВЛАДЕЕМ им), потому что исходный `text` после выхода из
/// функции может исчезнуть, а карта должна пережить его. Значит, каждое слово нужно
/// скопировать в собственную `String` (например, через `.to_string()`).
///
/// Пример: `"a b a"` → `{"a": 2, "b": 1}`. Для пустого/пробельного текста — пустая карта.
pub fn word_count(text: &str) -> HashMap<String, usize> {
    let mut map = HashMap::new();
    for word in text.split_whitespace() {
        *map.entry(word.to_string()).or_insert(0) += 1;
    }
    map
}

/// Склеить слова `words` в одну строку, вставляя `sep` между соседними.
///
/// Ведёт себя как `["a", "b", "c"]` + `", "` → `"a, b, c"`: разделитель ставится
/// МЕЖДУ элементами, но не до первого и не после последнего.
///
/// Граничные случаи: пустой срез → `""`; ровно один элемент → сам этот элемент без
/// разделителя.
///
/// Здесь и `words`, и `sep` — заимствования (`&[&str]`, `&str`): мы только читаем чужие
/// данные. А результат — новая `String`, которой владеет вызывающий код.
pub fn join_words(words: &[&str], sep: &str) -> String {
    words.join(sep)
}
