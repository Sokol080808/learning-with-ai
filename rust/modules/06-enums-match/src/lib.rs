/// Арифметическое выражение над числами `f64`.
///
/// Варианты:
/// - `Num(f64)` — числовой листовой узел;
/// - `Add(Box<Expr>, Box<Expr>)` — сложение двух подвыражений;
/// - `Sub(Box<Expr>, Box<Expr>)` — вычитание;
/// - `Mul(Box<Expr>, Box<Expr>)` — умножение;
/// - `Div(Box<Expr>, Box<Expr>)` — деление (может вернуть `None` при делении на ноль).
///
/// `Box` нужен потому, что рекурсивный `enum` без него имел бы бесконечный размер.
#[derive(Debug, Clone, PartialEq)]
pub enum Expr {
    Num(f64),
    Add(Box<Expr>, Box<Expr>),
    Sub(Box<Expr>, Box<Expr>),
    Mul(Box<Expr>, Box<Expr>),
    Div(Box<Expr>, Box<Expr>),
}

/// Вычисляет значение выражения.
///
/// Контракт:
/// - возвращает `Some(результат)`, если вычисление возможно;
/// - возвращает `None`, если **где-либо** в дереве встречается деление на ноль —
///   `None` «пузырится» наверх через всё дерево рекурсии.
///
/// Реализовано через рекурсивный `match` и оператор `?` для пропаганды `None`.
pub fn eval(e: &Expr) -> Option<f64> {
    match e {
        Expr::Num(n) => Some(*n),
        Expr::Add(l, r) => Some(eval(l)? + eval(r)?),
        Expr::Sub(l, r) => Some(eval(l)? - eval(r)?),
        Expr::Mul(l, r) => Some(eval(l)? * eval(r)?),
        Expr::Div(l, r) => {
            let lv = eval(l)?;
            let rv = eval(r)?;
            safe_div(lv, rv)
        }
    }
}

