/// Разбирает строку вида `"name:count"` в пару `(name, count)`.
///
/// Контракт:
/// - вход `"alpha:7"` → `Some(("alpha".to_string(), 7))`;
/// - нет двоеточия (`"alpha"`) → `None`;
/// - суффикс не парсится в `u32` (`"alpha:x"`, `"alpha:-1"`, `"alpha:1.5"`) → `None`;
/// - пустая строка `""` → `None`;
/// - **пустое имя** (`":7"`) → `None` (имя обязано быть непустым — решено и задокументировано);
/// - лишнее двоеточие (`"a:b:1"`) → `None`, потому что `split_once(':')` режет по
///   **первому** двоеточию и даёт суффикс `"b:1"`, который не парсится в `u32`.
///
/// ПОЧЕМУ так написано: эталон демонстрирует две идиомы «достань-или-выйди».
/// `let-else` для разреза — если двоеточия нет, сразу `return None`, без вложенности.
/// `if let Ok(n) = ... { Some(..) } else { None }` для разбора числа — содержательная
/// ветка плюс запасной путь. «Счастливый путь» идёт ровной прямой, провалы отстреливаются.
pub fn parse_label(s: &str) -> Option<(String, u32)> {
    // let-else: нет двоеточия → выходим немедленно.
    let Some((name, digits)) = s.split_once(':') else {
        return None;
    };
    // Имя обязано быть непустым (контракт).
    if name.is_empty() {
        return None;
    }
    // if let ... else: суффикс разбираем в u32 или None.
    if let Ok(n) = digits.parse::<u32>() {
        Some((name.to_string(), n))
    } else {
        None
    }
}
