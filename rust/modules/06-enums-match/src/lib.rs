/// Геометрическая фигура: либо круг с радиусом, либо прямоугольник со сторонами.
///
/// Это `enum` с данными в вариантах:
/// - `Circle(f64)`     — радиус;
/// - `Rect(f64, f64)`  — ширина и высота.
#[derive(Debug, Clone, Copy, PartialEq)]
pub enum Shape {
    Circle(f64),
    Rect(f64, f64),
}

impl Shape {
    /// Площадь фигуры.
    ///
    /// Контракт:
    /// - для `Circle(r)` вернуть `π · r · r` (используй `std::f64::consts::PI`);
    /// - для `Rect(w, h)` вернуть `w · h`.
    ///
    /// Реализуй через `match self { ... }`, извлекая данные из вариантов.
    pub fn area(&self) -> f64 {
        match self {
            Self::Circle(r) => std::f64::consts::PI * r * r,
            Self::Rect(w, h) => w * h,
        }
    }
}

/// Безопасное деление `a / b`.
///
/// Контракт:
/// - если `b == 0.0` — вернуть `None` (деление на ноль не определено);
/// - иначе — вернуть `Some(a / b)`.
///
/// Смысл `Option`: «результата может не быть», и это видно прямо в типе.
pub fn safe_div(a: f64, b: f64) -> Option<f64> {
    if b == 0.0 {
        None
    } else {
        Some(a / b)
    }
}

/// Первое чётное число в срезе.
///
/// Контракт:
/// - вернуть `Some(n)`, где `n` — первое (по порядку) чётное число в `v`;
/// - если чётных нет или срез пуст — вернуть `None`.
///
/// `v: &[i64]` — срез по ссылке: мы только читаем чужие данные, не владея ими.
pub fn first_even(v: &[i64]) -> Option<i64> {
    for &n in v {
        if n % 2 == 0 {
            return Some(n);
        }
    }
    None
}

/// Текстовое описание необязательного числа.
///
/// Контракт:
/// - для `Some(n)` вернуть строку `"value: N"` (где `N` — само число), напр. `"value: 7"`;
/// - для `None` вернуть строку `"nothing"`.
///
/// Удобно через `match o { Some(n) => ..., None => ... }` и `format!`.
pub fn describe(o: Option<i64>) -> String {
    match o {
        Some(n) => format!("value: {n}"),
        None => "nothing".to_string(),
    }
}

