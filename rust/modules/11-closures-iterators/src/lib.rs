/// Сумма квадратов всех элементов среза — ЧЕРЕЗ цепочку итераторов,
/// без ручного цикла `for` и без изменяемого аккумулятора.
///
/// Контракт:
/// - `sum_of_squares(&[1, 2, 3])` == `14`  (1 + 4 + 9);
/// - `sum_of_squares(&[])`        == `0`;
/// - `sum_of_squares(&[-2, 5])`   == `29`  (4 + 25).
///
/// Идея: `v.iter()` даёт итератор по `&i64`. Адаптер `map` лениво превращает
/// каждый элемент в его квадрат, а `sum()` — это потребитель, который
/// «прокручивает» итератор и складывает значения.
pub fn sum_of_squares(v: &[i64]) -> i64 {
    v.iter().map(|x| x * x).sum()
}

/// Возвращает НОВЫЙ вектор только из чётных элементов среза, сохраняя их порядок.
///
/// Контракт:
/// - `evens(&[1, 2, 3, 4])` == `vec![2, 4]`;
/// - `evens(&[1, 3, 5])`    == `vec![]`;
/// - `evens(&[0, -2, 7, -8])` == `vec![0, -2, -8]`  (ноль чётный, знак не важен).
///
/// Идея: `filter` лениво пропускает дальше только те элементы, для которых
/// предикат истинен, а `collect()` материализует результат в `Vec<i64>`.
/// Осторожно с типом: `iter()` отдаёт `&i64`, поэтому в предикате элемент —
/// ссылка, а в `Vec<i64>` нужны значения (пригодится `copied()`/`cloned()`
/// или разыменование).
pub fn evens(v: &[i64]) -> Vec<i64> {
    v.iter().filter(|x| *x % 2 == 0).copied().collect()
}

/// Превращает срез слов в вектор их длин (в байтах) с сохранением порядка.
///
/// Контракт:
/// - `word_lengths(&["a", "bb", "ccc"])` == `vec![1, 2, 3]`;
/// - `word_lengths(&[])`                 == `vec![]`;
/// - `word_lengths(&["", "hello"])`      == `vec![0, 5]`.
///
/// Идея: классический `map` — для каждого `&str` берём его длину `len()`,
/// получая `usize`, и собираем в `Vec<usize>`. Тип элемента итератора —
/// `&&str` (ссылка на элемент среза `&str`); вызов метода `.len()` на нём
/// работает благодаря авто-разыменованию.
pub fn word_lengths(words: &[&str]) -> Vec<usize> {
    words.iter().map(|w| w.len()).collect()
}

