/// Применяет функцию (или замыкание) `f` к аргументу `x` и возвращает результат.
///
/// Контракт:
/// - `apply(|n| n + 1, 41)` == `42`;
/// - `apply(|n| n * n, 9)`  == `81`.
///
/// Обобщённый параметр `F: Fn(i64) -> i64` означает: сюда можно передать
/// что угодно вызываемое — обычную функцию, замыкание без захвата, замыкание
/// с захватом окружения по ссылке. Граница `Fn` (а не `FnMut`/`FnOnce`)
/// обещает, что вызов не меняет окружение и возможен сколько угодно раз.
pub fn apply<F: Fn(i64) -> i64>(f: F, x: i64) -> i64 {
    f(x)
}

