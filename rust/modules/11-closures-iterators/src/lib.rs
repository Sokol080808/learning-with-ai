/// Адаптер итератора, реализующий кодирование длин серий (Run-Length Encoding).
///
/// Оборачивает любой `Iterator<Item = i64>` и выдаёт пары `(значение, количество)`:
/// каждая пара описывает один непрерывный «прогон» одного и того же значения.
///
/// Контракт:
/// - вход `[1, 1, 2, 3, 3, 3]` → `[(1, 2), (2, 1), (3, 3)]`;
/// - пустой вход              → итератор сразу заканчивается (`next` → `None`);
/// - одиночный элемент `[7]`   → `[(7, 1)]`.
///
/// Внутренняя логика: при создании адаптер «смотрит вперёд» на один элемент
/// (`lookahead`). При каждом вызове `next` он тянет элементы из внутреннего
/// итератора до тех пор, пока они совпадают с заглянутым вперёд значением,
/// считая их. Когда встречается другое значение (или итератор кончается),
/// адаптер возвращает накопленную пару и сохраняет новое значение как следующий
/// lookahead.
pub struct RunLengthEncoder<I> {
    /// Внутренний итератор-источник.
    inner: I,
    /// Следующий элемент, который уже был извлечён из `inner`, но ещё не выдан.
    /// `None` означает, что внутренний итератор исчерпан.
    lookahead: Option<i64>,
}

impl<I: Iterator<Item = i64>> RunLengthEncoder<I> {
    /// Создаёт новый `RunLengthEncoder`, оборачивая итератор `inner`.
    ///
    /// Сразу вытаскивает первый элемент в `lookahead`, чтобы `next` работал
    /// единообразно: если `inner` пуст — `lookahead` сразу `None`.
    pub fn new(mut inner: I) -> Self {
        let lookahead = inner.next();
        RunLengthEncoder { inner, lookahead }
    }
}

impl<I: Iterator<Item = i64>> Iterator for RunLengthEncoder<I> {
    type Item = (i64, usize);

    /// Возвращает следующую пару `(значение, количество)` или `None`, если
    /// итератор исчерпан.
    ///
    /// Алгоритм:
    /// 1. Если `lookahead` пуст — итератор закончился, возвращаем `None`.
    /// 2. Берём `current = lookahead`, выставляем счётчик `count = 1`.
    /// 3. Тянем элементы из `inner`: пока следующий == `current`, увеличиваем
    ///    `count` и продвигаем `lookahead`. При первом несовпадении
    ///    (или при `None`) выходим из цикла.
    /// 4. Возвращаем `Some((current, count))`.
    fn next(&mut self) -> Option<Self::Item> {
        // Шаг 1: если lookahead пуст — конец итератора.
        let current = self.lookahead?;

        // Шаг 2: начинаем подсчёт текущего прогона.
        let mut count = 1usize;

        // Шаг 3: тянем из inner, пока элементы совпадают с current.
        loop {
            match self.inner.next() {
                Some(val) if val == current => {
                    count += 1;
                    // lookahead по-прежнему указывает на current (не обновляем)
                }
                next_val => {
                    // Либо другое значение, либо итератор кончился.
                    // Сохраняем его как новый lookahead.
                    self.lookahead = next_val;
                    break;
                }
            }
        }

        // Шаг 4: выдаём накопленную пару.
        Some((current, count))
    }
}

