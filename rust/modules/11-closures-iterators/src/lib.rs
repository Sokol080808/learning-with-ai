/// Возвращает преобразователь `i64 -> i64`, выбранный по флагу:
/// `double == true` → удваивает аргумент, `double == false` → меняет знак.
///
/// Контракт:
/// - `make_transformer(true)(5)`  == `10`;
/// - `make_transformer(false)(5)` == `-5`;
/// - результат можно вызывать сколько угодно раз (граница `Fn`).
///
/// ПОЧЕМУ `Box<dyn Fn(...)>`, а не `impl Fn(...)`: две ветви возвращают замыкания
/// РАЗНОГО типа (у каждого свой анонимный тип, придуманный компилятором).
/// `impl Fn` обязывает все пути возврата отдавать ОДИН и тот же конкретный тип —
/// поэтому он тут не подходит. `Box<dyn Fn>` стирает тип: оба замыкания кладутся
/// в кучу за общим указателем `dyn Fn(i64) -> i64`, и функция возвращает один тип
/// для обеих веток. Цена — аллокация в куче и косвенный вызов через vtable.
pub fn make_transformer(double: bool) -> Box<dyn Fn(i64) -> i64> {
    if double {
        Box::new(|x| x * 2)
    } else {
        Box::new(|x| -x)
    }
}

/// Применяет каждое замыкание из среза к `x` и собирает результаты в `Vec`,
/// сохраняя порядок замыканий.
///
/// Контракт:
/// - `apply_all(&[], 7)` == `vec![]`;
/// - `apply_all(&[make_transformer(true), make_transformer(false)], 3)`
///   == `vec![6, -3]`;
/// - порядок результатов совпадает с порядком замыканий в срезе.
///
/// Идея: `fns.iter()` отдаёт `&Box<dyn Fn(i64) -> i64>`. Вызов `f(x)` работает
/// через авто-разыменование (`Box` и `&` снимаются до `dyn Fn`), а `map` лениво
/// превращает каждое замыкание в его результат на `x`. `collect()` материализует
/// итог в `Vec<i64>`.
pub fn apply_all(fns: &[Box<dyn Fn(i64) -> i64>], x: i64) -> Vec<i64> {
    fns.iter().map(|f| f(x)).collect()
}
