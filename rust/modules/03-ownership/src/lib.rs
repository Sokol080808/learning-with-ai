// Подсказка по чтению сигнатур: смотри, что функция БЕРЁТ во владение, а что — нет.
//   v: Vec<i64>      — взяли вектор во владение (вызвавший его больше не использует);
//   s: String        — взяли строку во владение;
//   v: &Vec<i64>     — только одолжили ссылку на чтение, владение осталось у вызвавшего.
// Это не формальность: тип в сигнатуре — это договор о том, кто отвечает за значение.

/// Складывает все элементы вектора и возвращает сумму.
///
/// Контракт: функция ПРИНИМАЕТ ВЛАДЕНИЕ вектором `v`. После вызова вызывающий код
/// больше не может использовать переданный вектор — владение перешло сюда, и в конце
/// тела `v` будет уничтожен (drop). Для пустого вектора сумма равна `0`.
pub fn sum_vec(v: Vec<i64>) -> i64 {
    v.into_iter().sum()
}

/// Склеивает две строки в одну: сначала `a`, затем `b`.
///
/// Контракт: функция ПОТРЕБЛЯЕТ обе строки (берёт во владение и `a`, и `b`).
/// Это позволяет переиспользовать буфер `a` без лишнего выделения памяти, дописав
/// в него `b`. Вызывающий код после этого ни `a`, ни `b` использовать не сможет.
pub fn concat_strings(a: String, b: String) -> String {
    let mut result = a;
    result.push_str(&b);
    result
}

/// Повторяет строку `s` ровно `times` раз и возвращает результат.
///
/// Контракт: функция ПОТРЕБЛЯЕТ строку `s` (берёт её во владение). При `times == 0`
/// возвращается пустая строка. Например, `repeat_string("ab".to_string(), 3)` даёт
/// `"ababab"`.
pub fn repeat_string(s: String, times: usize) -> String {
    s.repeat(times)
}

/// Возвращает НОВЫЙ вектор: копию `v` с дописанным в конец элементом `x`.
///
/// Контракт: функция НЕ забирает владение и НЕ меняет оригинал. Она лишь ОДАЛЖИВАЕТ
/// `v` через ссылку `&Vec<i64>` (только чтение), делает КЛОН и пушит `x` уже в клон.
/// Поэтому после вызова исходный вектор у вызывающего остаётся целым и неизменным —
/// в этом весь смысл клонирования вместо перемещения.
pub fn clone_and_push(v: &Vec<i64>, x: i64) -> Vec<i64> {
    let mut out = v.clone();
    out.push(x);
    out
}
