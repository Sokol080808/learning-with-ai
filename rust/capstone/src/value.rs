//! Модуль `value` — тип хранимого значения.
//!
//! В настоящем key-value хранилище (как Redis) под одним ключом может лежать значение
//! РАЗНОГО рода: просто строка, список строк или хеш (словарь поле→значение). В Rust
//! «одно из нескольких, и у каждого свои данные» — это `enum` с данными в вариантах
//! (модуль 06). Здесь ты его и объявляешь.

use std::collections::HashMap;

/// Значение, хранимое под ключом в [`crate::Database`].
///
/// Три варианта, и они не путаются между собой — компилятор через `match` заставит
/// обработать каждый:
/// - [`Value::Str`]  — одиночная строка (команды `SET`/`GET`);
/// - [`Value::List`] — упорядоченный список строк (команды `LPUSH`/`LRANGE`/`LLEN`);
/// - [`Value::Hash`] — словарь «поле → значение» (команды `HSET`/`HGET`/`HKEYS`).
///
/// ПОЧЕМУ `enum`, а не три отдельных `HashMap`: один ключ — ровно один тип значения,
/// и сам тип значения «носит» свои данные. Это и есть типизированное хранилище: попытка
/// сделать `LPUSH` по ключу-строке — логическая ошибка, и её должно быть видно (вернётся
/// `Err`, см. [`crate::Database::lpush`]).
///
/// `#[derive(...)]` ниже даёт бесплатно: `Debug` (печать в тестах/отладке), `Clone`
/// (скопировать значение), `PartialEq` (сравнивать в `assert_eq!`).
#[derive(Debug, Clone, PartialEq)]
pub enum Value {
    /// Одиночная строка.
    Str(String),
    /// Список строк (порядок важен).
    List(Vec<String>),
    /// Словарь «поле → значение».
    Hash(HashMap<String, String>),
}

