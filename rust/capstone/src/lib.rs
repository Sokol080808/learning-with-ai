//! # minidb — конкурентное in-memory хранилище с типами значений
//!
//! Это КАПСТОУН. Здесь нет пошаговых инструкций «сделай раз, сделай два». Есть
//! **спецификация** (этот файл + документация над каждой сигнатурой) и **приёмочные
//! тесты** (`tests/acceptance.rs`), которые описывают требуемое поведение. Тела всех
//! функций сейчас — `todo!()`: крейт **компилируется**, но тесты **красные**. Твоя
//! задача — реализовать тела так, чтобы зелёным стал каждый тест, не трогая ни
//! сигнатуры здесь, ни вызовы в тестах.
//!
//! Архитектура (рекомендуемая, но финальное слово за тобой):
//! - [`value`]   — тип значения [`Value`] (строка / список / хеш) — это твой `enum`;
//! - [`db`]      — однопоточная [`Database`] поверх `HashMap<String, Value>`;
//! - [`command`] — разбор и исполнение текстовых команд: [`execute`];
//! - [`shared`]  — потокобезопасная обёртка [`SharedDb`] на `Arc<Mutex<Database>>`.
//!
//! Что задействует капстоун из курса:
//! - владение и заимствование (`&self` / `&mut self`, `String` против `&str`);
//! - `enum` + `match` (модуль 06) — варианты [`Value`];
//! - `Result` и `?` (модуль 07) — операции, которые могут не справиться, и I/O;
//! - `Vec` / `String` / `HashMap` (модуль 08) — вся структура хранилища;
//! - обобщения и трейты (модуль 09) — `Send + Sync`, дженерик-границы в `thread::spawn`;
//! - умные указатели (модуль 13) и **потоки** — `Arc<Mutex<T>>`, разделяемое владение.
//!
//! Подробный план по майлстоунам — в `README.md` рядом с этим файлом.

// Крейт состоит из четырёх модулей. Их файлы лежат в src/. Публичный API крейта
// собирается через `pub use` ниже — поэтому в тестах достаточно `use minidb::*;`.
pub mod command;
pub mod db;
pub mod shared;
pub mod value;

// Реэкспорт публичного API в корень крейта. Благодаря этому пользователь пишет
// `minidb::Value`, `minidb::Database`, `minidb::execute`, `minidb::SharedDb`, а не
// длинные пути вроде `minidb::value::Value`.
pub use command::execute;
pub use db::Database;
pub use shared::SharedDb;
pub use value::Value;
