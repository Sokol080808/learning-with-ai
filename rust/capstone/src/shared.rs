//! Модуль `shared` — потокобезопасная обёртка [`SharedDb`].
//!
//! Это кульминация капстоуна и главная новая тема: **бесстрашная конкурентность**.
//! [`crate::Database`] сама по себе НЕ потокобезопасна — её нельзя одновременно менять
//! из двух потоков. Стандартный рецепт Rust:
//!
//! ```text
//! Arc<Mutex<Database>>
//!  │     │      └── сами данные
//!  │     └── Mutex: в любой момент к данным имеет доступ только ОДИН поток
//!  └── Arc: разделяемое владение — несколько потоков «держат» одно хранилище
//! ```
//!
//! - `Arc<T>` (Atomically Reference Counted, модуль 13) — как `Rc`, но счётчик ссылок
//!   атомарный, поэтому его МОЖНО передавать между потоками. `Arc::clone` не копирует
//!   данные — лишь увеличивает счётчик; все клоны указывают на одно хранилище.
//! - `Mutex<T>` — замок: `lock()` даёт эксклюзивный доступ; пока один поток держит
//!   замок, остальные ждут. Так гонка данных становится невозможной — и это проверяет
//!   компилятор через трейты `Send`/`Sync` (модуль 09), а не «дисциплина программиста».
//!
//! Именно поэтому [`SharedDb`] обязана быть `Send + Sync` — иначе `thread::spawn` её
//! просто не примет (см. тест на конкурентность).

