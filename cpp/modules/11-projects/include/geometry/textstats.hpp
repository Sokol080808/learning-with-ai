#pragma once
#include <string>
#include <string_view>

// Мини-«библиотека» textstats: живёт в собственном пространстве имён `textstats`,
// чтобы её имена не сталкивались с `geometry`. Объявления — здесь (интерфейс),
// тела — в src/textstats.cpp (реализация). Это и есть разделение интерфейс/реализация
// внутри одной единицы сборки (статической библиотеки модуля 11).
namespace textstats {

// Количество слов: непрерывные последовательности непробельных символов,
// разделённые пробелами/табами/переводами строк. Пустая строка → 0.
int word_count(std::string_view text);

// Длина самого длинного слова (в байтах). Пустая строка → 0.
int longest_word(std::string_view text);

// Перевести все ASCII-буквы в верхний регистр; остальные символы не трогать.
// Возвращает НОВУЮ строку, исходную не меняет.
std::string to_upper(std::string_view text);

}  // namespace textstats
