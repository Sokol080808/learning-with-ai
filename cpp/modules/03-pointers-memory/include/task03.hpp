void reverse_with_pointers(int* arr, int n);

// Задание 7. Ручной динамический массив на new[]/delete[].
// Класс владеет буфером из n int на куче. Никаких std::vector внутри —
// в этом и смысл: понять парность new[]/delete[] и RAII «своими руками».
class DynArray {
public:
    // Выделить на куче буфер на n элементов, заполнив их значением fill.
    // n == 0 — допустимо: буфера нет (или нулевой), size() == 0.
    explicit DynArray(int n, int fill = 0);

    // Освободить буфер через delete[] (парно к new[] в конструкторе).
    ~DynArray();

    // Количество элементов.
    int size() const;

    // Доступ к i-му элементу (две перегрузки: для чтения и для записи).
    // Если i вне диапазона [0, size()) — бросить std::out_of_range.
    int& at(int i);
    int at(int i) const;

    // Сумма всех элементов (0 для пустого массива).
    int sum() const;

    // Установить ВСЕ элементы в value.
    void fill(int value);

private:
    int* data_;
    int  size_;
};

// Задание 8. Демонстрация глубокого vs поверхностного копирования.
// Класс владеет одним int на куче. Конструктор/деструктор уже даны;
// тебе предстоит сделать копирование ГЛУБОКИМ (правило трёх).
class OwnedInt {
public:
    // Выделить на куче один int со значением value.
    explicit OwnedInt(int value);

    // Глубокое копирование: новый объект получает СВОЙ буфер на куче
    // с тем же значением (а не общий указатель с исходным).
    OwnedInt(const OwnedInt& other);

    // Глубокое присваивание (тоже свой буфер; не течь и не делить память).
    OwnedInt& operator=(const OwnedInt& other);

    // Освободить свой буфер.
    ~OwnedInt();

    // Прочитать значение.
    int get() const;

    // Записать новое значение (в собственный буфер этого объекта).
    void set(int value);

    // Адрес внутреннего буфера — нужен тестам, чтобы проверить,
    // что у копии указатель ДРУГОЙ (то есть копирование глубокое).
    const int* raw() const;

private:
    int* ptr_;
};
