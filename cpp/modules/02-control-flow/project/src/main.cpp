// Готовое CLI-приложение калькулятора. Тебе его править не нужно — изучи, как оно
// устроено, и как использует твою функцию calc(). Здесь же показан простой способ
// проверить ввод ПЕРЕД вызовом calc (полноценная обработка ошибок — модуль 09).
#include <iostream>
#include "calc.hpp"

int main() {
    double a, b;
    char op;
    std::cout << "Введите выражение в формате: a op b  (например: 3 * 4)\n";
    if (!(std::cin >> a >> op >> b)) {
        std::cerr << "Не удалось разобрать ввод.\n";
        return 1;
    }

    if (op != '+' && op != '-' && op != '*' && op != '/') {
        std::cerr << "Неизвестная операция: " << op << "\n";
        return 1;
    }
    if (op == '/' && b == 0.0) {
        std::cerr << "Деление на ноль.\n";
        return 1;
    }

    std::cout << a << " " << op << " " << b << " = " << calc(a, op, b) << "\n";
    return 0;
}
