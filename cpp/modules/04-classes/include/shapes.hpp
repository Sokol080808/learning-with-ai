#pragma once
#include <string>
#include <cmath>

// ──────────────────────────────────────────────────────────────────────────
// Задание 5. Иерархия фигур (Shape hierarchy).
//
// Демонстрирует:
//   * абстрактный базовый класс с чисто виртуальными методами;
//   * виртуальный диспатч (вызов переопределения через базовый указатель/ссылку);
//   * виртуальный деструктор — без него delete через Shape* давал бы UB;
//   * override и final как инструменты безопасности при наследовании;
//   * полиморфизм через std::unique_ptr<Shape>.
// ──────────────────────────────────────────────────────────────────────────

// Абстрактный базовый класс. Нельзя создать объект типа Shape напрямую —
// у него есть хотя бы один чисто виртуальный метод (= 0).
class Shape {
public:
    // Виртуальный деструктор ОБЯЗАТЕЛЕН для любого полиморфного базового класса.
    // Без него `delete ptr` (где ptr — Shape*) вызывает UB: компилятор не знает,
    // что нужно вызвать деструктор производного класса — просто вызывает ~Shape().
    virtual ~Shape() = default;

    // Чисто виртуальные методы — контракт, который ОБЯЗАН выполнить каждый наследник.
    // Класс с хотя бы одним pure-virtual — абстрактный: его нельзя инстанциировать.
    virtual double area()      const = 0;
    virtual double perimeter() const = 0;
    virtual std::string name() const = 0;
};

// ──────────────────────────────────────────────────────────────────────────
// Circle — окружность радиуса r.
//
// override после подписи: компилятор проверит, что метод действительно
// переопределяет виртуальный метод базового класса. Опечатка в имени
// или неверная сигнатура — ошибка компиляции, а не тихая новая перегрузка.
// ──────────────────────────────────────────────────────────────────────────
class Circle final : public Shape {
public:
    explicit Circle(double r);

    double area()      const override;  // π·r²
    double perimeter() const override;  // 2·π·r
    std::string name() const override;  // "Circle"

    double radius() const;

private:
    double r_;
};

// ──────────────────────────────────────────────────────────────────────────
// Rectangle — прямоугольник со сторонами w × h.
// ──────────────────────────────────────────────────────────────────────────
class Rectangle final : public Shape {
public:
    Rectangle(double w, double h);

    double area()      const override;  // w·h
    double perimeter() const override;  // 2·(w+h)
    std::string name() const override;  // "Rectangle"

    double width()  const;
    double height() const;

private:
    double w_;
    double h_;
};
