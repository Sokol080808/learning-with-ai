# Соглашения по формам тензоров (держи их в голове — половина багов тут):
#   X : (N, D)  — N объектов, у каждого D признаков.
#   w : (D,)    — по одному весу на признак.
#   b : float   — один общий сдвиг (скаляр).
#   y : (N,)    — целевые значения, по одному на объект.
#   y_pred : (N,) — предсказания, та же форма, что и y.

from __future__ import annotations

import numpy as np


def predict(X: np.ndarray, w: np.ndarray, b: float) -> np.ndarray:
    """Линейная модель: y_pred = X · w + b.

    Аргументы:
        X : np.ndarray формы (N, D) — матрица объектов (строка = объект, столбец = признак).
        w : np.ndarray формы (D,)   — вектор весов (по одному на признак).
        b : float                   — сдвиг (bias), один на всю модель.

    Возвращает:
        np.ndarray формы (N,) — предсказание для каждого из N объектов.

    Контракт:
        - Используй матрично-векторное умножение X @ w (форма (N, D) @ (D,) = (N,)),
          затем прибавь скаляр b ко всем элементам.
        - Никаких циклов по объектам не нужно — это одна строка на numpy.
    """
    return X @ w + b


def mse_loss(y_pred: np.ndarray, y: np.ndarray) -> float:
    """Среднеквадратичная ошибка (Mean Squared Error).

    Аргументы:
        y_pred : np.ndarray формы (N,) — предсказания модели.
        y      : np.ndarray формы (N,) — истинные значения.

    Возвращает:
        float — среднее значение квадратов ошибок: mean((y_pred - y) ** 2).

    Контракт:
        - Посчитай поэлементную разность (y_pred - y), возведи в квадрат, возьми среднее.
        - Результат — обычный python-float (приведи через float(...)), а не 0-мерный массив.
        - Loss >= 0 всегда; равен 0 только при идеальном совпадении.
    """
    return float(np.mean((y_pred - y) ** 2))


def gradients(
    X: np.ndarray, y: np.ndarray, w: np.ndarray, b: float
) -> tuple[np.ndarray, float]:
    """Градиенты MSE-loss по параметрам w и b.

    Аргументы:
        X : np.ndarray формы (N, D)
        y : np.ndarray формы (N,)   — истинные значения.
        w : np.ndarray формы (D,)   — текущие веса.
        b : float                   — текущий сдвиг.

    Возвращает:
        (dw, db), где
            dw : np.ndarray формы (D,) — градиент loss по w (та же форма, что и w).
            db : float                 — градиент loss по b (скаляр).

    Контракт (вывод формул — в README, секция «Идея 3»):
        Обозначим ошибку e = predict(X, w, b) - y   (форма (N,)).
        Тогда для loss = mean(e ** 2):
            dw = (2 / N) * (X.T @ e)     # форма (D,)
            db = (2 / N) * sum(e)        # скаляр
        где N = число объектов (X.shape[0]).
        - Сначала получи предсказания через predict (переиспользуй свою функцию).
        - db верни обычным float.
    """
    N = X.shape[0]
    e = predict(X, w, b) - y
    dw = (2.0 / N) * (X.T @ e)
    db = float((2.0 / N) * np.sum(e))
    return dw, db


def gd_step(
    w: np.ndarray, b: float, dw: np.ndarray, db: float, lr: float
) -> tuple[np.ndarray, float]:
    """Один шаг градиентного спуска: сдвигаем параметры ПРОТИВ градиента.

    Аргументы:
        w  : np.ndarray формы (D,) — текущие веса.
        b  : float                 — текущий сдвиг.
        dw : np.ndarray формы (D,) — градиент по w.
        db : float                 — градиент по b.
        lr : float                 — скорость обучения (learning rate), размер шага.

    Возвращает:
        (w_new, b_new) — обновлённые параметры тех же форм, что и на входе.

    Контракт:
        w_new = w - lr * dw      # форма (D,)
        b_new = b - lr * db      # скаляр
        - Знак МИНУС обязателен: градиент указывает в сторону РОСТА loss, а нам нужно вниз.
        - b_new верни обычным float.
    """
    return w - lr * dw, float(b - lr * db)
