import numpy as np


def vector_add(a: np.ndarray, b: np.ndarray) -> np.ndarray:
    """Поэлементная сумма двух numpy-массивов одинаковой формы.

    Контракт:
      * a и b — numpy-массивы одинаковой формы, например (D,).
      * Возвращает новый массив той же формы, где result[i] = a[i] + b[i].
      * Используй средства numpy (одна операция над массивами), а не цикл for.

    Пример: vector_add([1, 2, 3], [10, 20, 30]) -> [11, 22, 33].
    Формы: (D,) + (D,) -> (D,).
    """
    return a + b


def mean(xs: np.ndarray) -> float:
    """Среднее арифметическое всех элементов массива как питоновский float.

    Контракт:
      * xs — numpy-массив, например формы (N,).
      * Возвращает обычный питоновский float, равный (xs[0] + ... + xs[N-1]) / N.
      * Считай через numpy, но верни именно float (скаляр), а НЕ numpy-массив ранга 0.

    Пример: mean([2, 4, 6]) -> 4.0.
    Формы: (N,) -> () (массив схлопывается в одно число).
    """
    return float(np.mean(xs))


def mse_loss(pred: np.ndarray, target: np.ndarray) -> float:
    """Среднеквадратичная ошибка (MSE) между предсказаниями и целевыми значениями.

    Контракт:
      * pred и target — numpy-массивы одинаковой произвольной формы (любой ранг).
      * Возвращает обычный питоновский float:
            loss = mean((pred - target) ** 2)
        то есть среднее квадратов поэлементных разностей по ВСЕМ элементам.
      * Не пиши цикл for; одним-двумя numpy-выражениями.

    Примеры:
      mse_loss([3.0], [3.0])          -> 0.0       # нет ошибки
      mse_loss([0.0], [1.0])          -> 1.0       # отклонение 1
      mse_loss([1.0, 2.0], [3.0, 2.0]) -> 2.0     # ((1-3)^2 + (2-2)^2) / 2 = 2.0

    Формы: (...) , (...) -> float (скаляр).
    Связь с теорией: это «кит номер 3» — функция потерь, которая говорит, насколько
    предсказание модели далеко от правды. Градиентный спуск минимизирует именно её.
    """
    diff = pred - target
    return float(np.mean(diff ** 2))
