from typing import Callable

import numpy as np


def numerical_gradient(
    f: Callable[[np.ndarray], float],
    x: np.ndarray,
    eps: float = 1e-5,
) -> np.ndarray:
    """Численный градиент скалярной функции `f` в точке `x` (центральная разность).

    Параметры:
        f   — функция, принимающая массив формы `x.shape` и возвращающая ОДНО число
              (скаляр). Например `lambda v: np.sum(v ** 2)`.
        x   — точка, в которой считаем градиент; массив любой формы, например (D,).
        eps — величина возмущения по каждой координате (по умолчанию 1e-5).

    Что вернуть:
        Массив той же формы, что `x`. В каждой координате `i` лежит оценка
        частной производной `∂f/∂x_i`, посчитанная центральной разностью:

            ( f(x + eps·eᵢ) − f(x − eps·eᵢ) ) / (2·eps)

        где `eᵢ` — шаг только по i-й координате (остальные не трогаем).

    Контракт:
        - Возмущай КАЖДУЮ координату по очереди и каждый раз откатывай возмущение,
          чтобы следующая координата считалась от чистой точки `x`.
        - Не порть переданный `x`: после вызова он должен остаться неизменным
          (работай с копиями, приведёнными к float).
        - Делить нужно на (2·eps), а не на eps — это и есть «центральная» разность.
    """
    grad = np.zeros_like(x, dtype=float)
    x_work = x.astype(float).copy()
    for idx in np.ndindex(x.shape):
        orig = x_work[idx]
        x_work[idx] = orig + eps
        fp = f(x_work)
        x_work[idx] = orig - eps
        fm = f(x_work)
        x_work[idx] = orig
        grad[idx] = (fp - fm) / (2.0 * eps)
    return grad


def grad_sum_squares(x: np.ndarray) -> np.ndarray:
    """Аналитический градиент функции f(x) = sum(x**2).

    Параметры:
        x — массив любой формы, например (D,).

    Что вернуть:
        Массив той же формы, что `x`, равный 2·x (производная x² по x — это 2x).

    Контракт:
        - Форма результата совпадает с формой `x`.
        - Результат — float-массив; исходный `x` не меняем.
    """
    return 2.0 * x.astype(float)


def grad_of_affine(x: np.ndarray, w: np.ndarray) -> np.ndarray:
    """Аналитический градиент функции f(x) = dot(w, x) ПО x.

    Параметры:
        x — вектор-аргумент, форма (D,).
        w — вектор коэффициентов, форма (D,) (та же, что у x).

    Что вернуть:
        Массив формы (D,), равный `w`: f линейна по x, поэтому ∂f/∂x_i = w_i.

    Контракт:
        - Форма результата совпадает с формой `x` и `w`.
        - Верни именно копию `w` (не тот же самый объект, что пришёл), float.
    """
    return w.astype(float).copy()
