import numpy as np


def scale(x: np.ndarray, k: float) -> np.ndarray:
    """Умножить каждый элемент массива на скаляр k.

    Контракт:
      - вход x — любой numpy-массив (любой формы);
      - возвращает новый массив той же формы, что и x, где каждый элемент
        умножен на k;
      - чистая векторизация, без циклов.

    Пример: scale(np.array([1., 2., 3.]), 2.0) -> array([2., 4., 6.]).
    """
    return x * k


def row_normalize(x: np.ndarray) -> np.ndarray:
    """Поделить каждую строку матрицы на сумму её собственных элементов.

    Контракт:
      - вход x — двумерный массив формы (N, D);
      - возвращает массив той же формы (N, D), где каждая строка поделена на
        сумму своих элементов, так что сумма каждой строки результата равна 1;
      - используй axis и keepdims, чтобы broadcasting сработал корректно;
      - предполагается, что сумма каждой строки не равна нулю.

    Пример: для строки [1., 3.] (сумма 4) результат [0.25, 0.75].
    """
    return x / x.sum(axis=1, keepdims=True)


def add_bias(x: np.ndarray, b: np.ndarray) -> np.ndarray:
    """Прибавить вектор смещения b к каждой строке матрицы x через broadcasting.

    Контракт:
      - вход x — массив формы (N, D);
      - вход b — массив формы (D,);
      - возвращает массив формы (N, D): к каждой строке x прибавлен b;
      - используй broadcasting, без явных циклов.

    Пример: x=[[1,2],[3,4]], b=[10,20] -> [[11,22],[13,24]].
    """
    return x + b


def relu_np(x: np.ndarray) -> np.ndarray:
    """Поэлементный ReLU: max(0, x).

    Контракт:
      - вход x — любой numpy-массив;
      - возвращает новый массив той же формы: отрицательные элементы заменены
        на 0, неотрицательные — без изменений;
      - вход x не изменяется на месте;
      - без циклов.

    Пример: relu_np(np.array([-2., 0., 3.])) -> array([0., 0., 3.]).
    """
    return np.maximum(x, 0.0)


def standardize(x: np.ndarray) -> np.ndarray:
    """Z-нормализация признаков: вычесть среднее и поделить на std по каждому столбцу.

    Контракт:
      - вход x — двумерный массив формы (N, D): N объектов, D признаков;
      - для каждого признака (столбца) вычесть его среднее и поделить на его
        стандартное отклонение, так что у каждого признака результата среднее
        становится ~0, а стандартное отклонение ~1 (z-score);
      - возвращает новый массив той же формы (N, D), вход не мутируется;
      - чистая векторизация, без циклов; правильную ось выбери сам;
      - предполагается, что ни один столбец не постоянен (его std != 0).

    Это стандартный препроцессинг признаков и прямой предшественник
    batch normalization: там та же идея центрирования и масштабирования, но
    среднее/дисперсия считаются по батчу на каждом шаге обучения.

    Пример: для столбца со значениями [1., 3., 5.] (среднее 3, std 1.633...)
    результат столбца — [-1.224..., 0., 1.224...].
    """
    return (x - x.mean(axis=0)) / x.std(axis=0)
