def adam_step(
    params: np.ndarray,
    grads: np.ndarray,
    m: np.ndarray,
    v: np.ndarray,
    t: int,
    lr: float,
    b1: float,
    b2: float,
    eps: float,
) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Один шаг оптимизатора Adam (adaptive moment estimation).

    Идея: Adam держит ДВА сглаженных средних:
      - m — среднее самих градиентов (1-й момент, «куда в среднем идём»);
      - v — среднее КВАДРАТОВ градиентов (2-й момент, «насколько крупные градиенты»).
    Шаг по каждому параметру делится на sqrt(v): по «громким» координатам шаг
    автоматически уменьшается, по «тихим» — относительно увеличивается. Получается свой
    масштаб шага НА КАЖДЫЙ параметр.

    Поправка на смещение (bias correction): m и v стартуют с нулей, поэтому в начале они
    занижены. Делим на (1 - b1**t) и (1 - b2**t), чтобы это исправить. Здесь t — номер
    шага, начиная с 1 (на первом вызове t == 1).

    Формулы:
        m_new = b1 * m + (1 - b1) * grads
        v_new = b2 * v + (1 - b2) * (grads ** 2)
        m_hat = m_new / (1 - b1 ** t)
        v_hat = v_new / (1 - b2 ** t)
        params_new = params - lr * m_hat / (sqrt(v_hat) + eps)

    Аргументы:
        params: текущие параметры, форма (...,).
        grads:  градиент по params, той же формы.
        m:      1-й момент (в начале — нули), той же формы.
        v:      2-й момент (в начале — нули), той же формы.
        t:      номер текущего шага, целое >= 1.
        lr:     learning rate (типично 1e-3).
        b1:     коэффициент сглаживания 1-го момента (типично 0.9).
        b2:     коэффициент сглаживания 2-го момента (типично 0.999).
        eps:    маленькая добавка в знаменатель против деления на ноль (типично 1e-8).

    Возвращает:
        Кортеж (params_new, m_new, v_new) — все той же формы, что params.
        ВАЖНО: возвращай НЕпоправленные m_new и v_new (без bias correction);
        поправку используем только для вычисления шага этого вызова.
    """
