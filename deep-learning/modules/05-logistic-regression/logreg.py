# Это пять кирпичиков классификатора:
#   sigmoid          — превращает любое число в вероятность из (0, 1);
#   bce_loss         — бинарная кросс-энтропия: насколько вероятности расходятся с истиной;
#   predict_proba    — линейный слой xW+b, пропущенный через сигмоиду;
#   accuracy         — доля верно угаданных классов;
#   logreg_gradients — градиенты loss по весам w и по сдвигу b (для шага обучения).
#
# Формы тензоров (держи их в голове — половина багов в DL это перепутанные оси):
#   X: (N, D)   — N объектов, у каждого D признаков;
#   w: (D,)     — по одному весу на признак;
#   b: скаляр   — общий сдвиг;
#   y: (N,)     — истинные метки 0/1;
#   p: (N,)     — предсказанные вероятности класса 1.
#
# Нельзя пользоваться готовыми sigmoid/log_loss из чужих библиотек — пишем руками.
# Разрешён только numpy.

from __future__ import annotations

import numpy as np


def sigmoid(z: np.ndarray) -> np.ndarray:
    """Сигмоида, применённая поэлементно: σ(z) = 1 / (1 + e^{-z}).

    Превращает любое вещественное число в вероятность из открытого интервала (0, 1):
    большие положительные z → близко к 1, большие отрицательные z → близко к 0, z=0 → 0.5.

    Контракт:
      - z: np.ndarray произвольной формы (или скаляр-совместимый вход).
      - Возвращает np.ndarray ТОЙ ЖЕ формы со значениями строго в (0, 1).
      - Должна быть ЧИСЛЕННО СТАБИЛЬНОЙ: на больших по модулю z (например ±1000)
        не должно быть переполнения (inf/nan). Прямая формула 1/(1+exp(-z)) при
        очень отрицательном z даёт exp(огромное) = inf — это надо обойти.

    Подсказка по стабильности: для z >= 0 используй 1/(1+e^{-z}), а для z < 0 —
    эквивалентную форму e^{z}/(1+e^{z}). В обеих ветках под экспонентой стоит число <= 0,
    поэтому exp не переполняется. Ветки можно собрать через np.where или булевы маски.
    """
    z = np.asarray(z, dtype=float)
    result = np.empty_like(z)
    pos = z >= 0
    neg = ~pos
    result[pos] = 1.0 / (1.0 + np.exp(-z[pos]))
    ez = np.exp(z[neg])
    result[neg] = ez / (1.0 + ez)
    return result


def bce_loss(p: np.ndarray, y: np.ndarray) -> float:
    """Бинарная кросс-энтропия (средняя по объектам).

    Для одного объекта: L = -( y*log(p) + (1-y)*log(1-p) ).
    Идея: если истинный класс 1, штраф растёт, когда p мало; если класс 0 — когда p велико.
    Итоговый loss — СРЕДНЕЕ по всем N объектам.

    Контракт:
      - p: np.ndarray формы (N,) — предсказанные вероятности класса 1, значения в (0, 1).
      - y: np.ndarray формы (N,) — истинные метки 0.0/1.0.
      - Возвращает обычный python float (одно число).
      - Защита от log(0): прежде чем брать логарифм, ОБРЕЖЬ p в диапазон
        [eps, 1-eps] (например eps = 1e-12) через np.clip — иначе при p=0 или p=1
        получишь log(0) = -inf и весь loss станет nan.
    """
    eps = 1e-12
    p = np.clip(p, eps, 1.0 - eps)
    return float(np.mean(-(y * np.log(p) + (1.0 - y) * np.log(1.0 - p))))


def predict_proba(X: np.ndarray, w: np.ndarray, b: float) -> np.ndarray:
    """Вероятности класса 1 для каждого объекта: p = σ(X·w + b).

    Сначала линейный слой: z = X @ w + b даёт по одному числу (логиту) на объект.
    Затем сигмоида превращает логиты в вероятности.

    Формы:
      - X: (N, D), w: (D,), b: скаляр → z: (N,) → p: (N,).

    Контракт:
      - Возвращает np.ndarray формы (N,) со значениями в (0, 1).
      - Используй уже написанную тобой sigmoid (стабильность достанется бесплатно).
    """
    return sigmoid(X @ w + b)


def accuracy(p: np.ndarray, y: np.ndarray, thr: float = 0.5) -> float:
    """Доля верно классифицированных объектов (accuracy).

    Сначала переводим вероятности в жёсткие предсказания: класс = 1, если p >= thr, иначе 0.
    Затем считаем долю совпадений с истинными метками y.

    Контракт:
      - p: (N,) вероятности, y: (N,) метки 0/1, thr: порог (по умолчанию 0.5).
      - Возвращает python float в [0.0, 1.0]: (число верных) / N.
      - Сравнение «p >= thr» (не строгое), чтобы p ровно на пороге шло в класс 1.
    """
    preds = (p >= thr).astype(float)
    return float(np.mean(preds == y))


def logreg_gradients(
    X: np.ndarray, y: np.ndarray, w: np.ndarray, b: float
) -> tuple[np.ndarray, float]:
    """Градиенты средней BCE по параметрам логистической регрессии.

    Замечательный факт: для логистической регрессии (сигмоида + BCE) градиент имеет
    ту же простую форму, что и для линейной регрессии с MSE. Если ввести «ошибку»
    err = p - y (где p = predict_proba(X, w, b)), то:

        dw = (1/N) * Xᵀ · err        # форма (D,)
        db = (1/N) * Σ err           # скаляр (среднее ошибок)

    Эти dw, db — направление РОСТА loss; на шаге обучения ты будешь вычитать
    learning_rate * dw (и * db), чтобы loss убывал.

    Формы:
      - X: (N, D), y: (N,), w: (D,), b: скаляр.
      - err: (N,);  Xᵀ: (D, N);  Xᵀ·err: (D,) → dw: (D,);  db: скаляр.

    Контракт:
      - Возвращает кортеж (dw, db): dw — np.ndarray формы (D,), db — python float.
      - Внутри посчитай p через predict_proba (не дублируй формулу сигмоиды).
    """
    N = X.shape[0]
    p = predict_proba(X, w, b)  # (N,)
    err = p - y                  # (N,)
    dw = (X.T @ err) / N        # (D,)
    db = float(np.sum(err) / N)
    return dw, db


# ──────────────────────────────────────────────────────────────────────────────
# Мост к многоклассовой задаче: softmax + категориальная кросс-энтропия (CCE).
#
# Бинарный случай выше — это сигмоида (один логит) + BCE. Для K классов прямое
# обобщение: softmax (вектор логитов → распределение) + CCE (−log вероятности
# правильного класса). Ключ к численной стабильности обоих — log-sum-exp trick:
# вычесть максимум логита перед exp (softmax не меняется при сдвиге всех логитов
# на константу, зато exp больше не переполняется).
# ──────────────────────────────────────────────────────────────────────────────


def softmax(z: np.ndarray) -> np.ndarray:
    """Softmax — обобщение сигмоиды на K классов, численно стабильный.

    Превращает вектор логитов в распределение вероятностей (неотрицательные,
    суммируются в 1):

        softmax(z)_k = e^{z_k} / Σ_j e^{z_j}

    Контракт:
      - z: np.ndarray формы (K,) ИЛИ (N, K). Для 2-D входа softmax берётся
        ПОСТРОЧНО (нормировка по последней оси K); каждая строка → распределение.
      - Возвращает np.ndarray ТОЙ ЖЕ формы; каждая строка суммируется в 1.0.
      - ЧИСЛЕННО СТАБИЛЬНЫЙ: на больших логитах (например [1000, 1001, 1002])
        не должно быть inf/nan. Достигается вычитанием максимума по строке перед
        exp — softmax инвариантен к прибавлению константы ко всем логитам:
        softmax(z + c) = softmax(z), поэтому берём c = max(z) и e^{z−c} ≤ 1.
    """
    z = np.asarray(z, dtype=float)
    c = np.max(z, axis=-1, keepdims=True)          # (..., 1) — максимум по строке
    ez = np.exp(z - c)                              # все ≤ 1 → exp не переполняется
    return ez / np.sum(ez, axis=-1, keepdims=True)


def cce_loss(logits: np.ndarray, y_int: np.ndarray) -> float:
    """Категориальная кросс-энтропия ИЗ ЛОГИТОВ (средняя по объектам), стабильная.

    K-классовое обобщение BCE. Для одного объекта с истинным классом y:
        L = −log( softmax(logits)_y )
    Это «удивление» модели от того, что правильным оказался класс y, которому она
    дала вероятность softmax(logits)_y.

    Контракт:
      - logits: np.ndarray формы (N, K) — СЫРЫЕ логиты (не вероятности!).
      - y_int:  np.ndarray формы (N,) — целочисленные метки класса 0…K−1.
      - Возвращает python float — среднее −log(p_correct) по N объектам.
      - ЧИСЛЕННО СТАБИЛЬНАЯ через log-sum-exp: НЕ считаем log(softmax(...)) в лоб
        (там log маленького числа теряет точность), а напрямую
            log p_y = z_y − logsumexp(z),   logsumexp(z) = c + log Σ_j e^{z_j − c},
        где c = max_j z_j. Так нет ни деления на крошечное число, ни log(0).
        Это ровно то, что делает torch.nn.functional.cross_entropy (он тоже
        принимает логиты, а не вероятности).
    """
    logits = np.asarray(logits, dtype=float)
    y_int = np.asarray(y_int)
    N = logits.shape[0]

    c = np.max(logits, axis=-1, keepdims=True)                 # (N, 1)
    logsumexp = c[:, 0] + np.log(np.sum(np.exp(logits - c), axis=-1))  # (N,)
    z_correct = logits[np.arange(N), y_int]                    # (N,) — логит истинного класса
    log_p_correct = z_correct - logsumexp                      # (N,) — log softmax истинного класса
    return float(np.mean(-log_p_correct))
