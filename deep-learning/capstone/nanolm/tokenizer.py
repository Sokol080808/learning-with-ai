from typing import List


class CharTokenizer:
    """Токенизатор уровня символов: текст <-> список целочисленных индексов.

    Идея. Берём текст, собираем все уникальные символы, сортируем их — получаем
    стабильный алфавит. Каждому символу присваиваем номер (его позицию в
    отсортированном алфавите). Кодирование — замена символов на номера, декодиро-
    вание — обратная замена.

    Контракт:
      - build(text): построить алфавит по тексту. Алфавит — ОТСОРТИРОВАННЫЙ список
        уникальных символов (sorted(set(text))). Сортировка даёт детерминизм:
        один и тот же текст всегда даёт один и тот же словарь и те же индексы.
        Заведи два отображения: stoi (символ -> индекс) и itos (индекс -> символ).
      - encode(text) -> list[int]: каждый символ -> его индекс. Длина результата
        равна длине текста (один символ = один индекс).
      - decode(ids) -> str: каждый индекс -> его символ, склеить в строку.
      - vocab_size (свойство) -> int: размер алфавита (число уникальных символов).

    Главное свойство (round-trip): для любого текста из обученного алфавита
        decode(encode(text)) == text.

    Пример. text = "abca".
      sorted(set(text)) == ['a', 'b', 'c']  -> vocab_size == 3
      stoi == {'a': 0, 'b': 1, 'c': 2}
      encode("cab") == [2, 0, 1]
      decode([2, 0, 1]) == "cab"

    Замечание о форме. Токенизатор работает со строками и списками int — тензоров
    тут ещё нет. Превращение list[int] -> torch.Tensor делается уже в данных/обучении.
    """

    def __init__(self) -> None:
        # Пока build не вызван — пустой алфавит и пустые отображения.
        self.chars: List[str] = []
        self.stoi: dict = {}
        self.itos: dict = {}

    def build(self, text: str) -> "CharTokenizer":
        """Построить алфавит и отображения по тексту. Верни self (для чейнинга).

        chars = sorted(set(text)); stoi = {ch: i ...}; itos = обратное.
        """
        self.chars = sorted(set(text))
        self.stoi = {ch: i for i, ch in enumerate(self.chars)}
        self.itos = {i: ch for i, ch in enumerate(self.chars)}
        return self

    def encode(self, text: str) -> List[int]:
        """Текст -> список индексов (по одному индексу на символ)."""
        return [self.stoi[ch] for ch in text]

    def decode(self, ids: List[int]) -> str:
        """Список индексов -> текст (склей символы)."""
        return "".join(self.itos[int(i)] for i in ids)

    @property
    def vocab_size(self) -> int:
        """Размер алфавита (число уникальных символов)."""
        return len(self.chars)
