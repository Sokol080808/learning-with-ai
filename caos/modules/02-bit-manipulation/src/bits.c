#include "bits.h"

// Контракт: вернуть значение i-го бита числа x (0 или 1).
// Биты нумеруются справа налево, начиная с 0: самый правый (младший) бит — это бит 0.
int get_bit(uint32_t x, int i) {
    return (int)((x >> i) & 1u);
}

// Контракт: вернуть x, в котором i-й бит установлен в 1. Остальные биты не меняем.
uint32_t set_bit(uint32_t x, int i) {
    return x | (1u << i);
}

// Контракт: вернуть x, в котором i-й бит сброшен в 0. Остальные биты не меняем.
uint32_t clear_bit(uint32_t x, int i) {
    return x & ~(1u << i);
}

// Контракт: вернуть x, в котором i-й бит инвертирован (0->1, 1->0). Остальные не меняем.
uint32_t toggle_bit(uint32_t x, int i) {
    return x ^ (1u << i);
}

// Контракт: вернуть 1, если x — степень двойки и не ноль (ровно один установленный бит), иначе 0.
int is_power_of_two(uint32_t x) {
    return (x != 0u) && ((x & (x - 1u)) == 0u) ? 1 : 0;
}

// Контракт: вернуть x с переставленным порядком 4 байт.
// Байт 0 (младший) меняется местами с байтом 3 (старшим), байт 1 — с байтом 2.
uint32_t reverse_bytes(uint32_t x) {
    return ((x & 0x000000FFu) << 24) |
           ((x & 0x0000FF00u) <<  8) |
           ((x & 0x00FF0000u) >>  8) |
           ((x & 0xFF000000u) >> 24);
}
