// Демо-приложение Tiny VM (рабочее, НЕ стаб — трогать не обязательно).
//
// Оно зашивает одну маленькую программу для виртуальной машины, запускает её
// через твой vm_run и печатает результат. Пока vm_run — заглушка, демо честно
// напишет, что произошла ошибка. Как только реализуешь машину — увидишь число.
//
// Запуск (после сборки `./caos/run.sh`):
//     ./caos/build/capstone/vm_demo
//
// Это «отладочная площадка»: меняй программу prog[] ниже и смотри, что считает
// твоя машина. Тесты проверяют корректность строго, а тут можно поиграть руками.

#include <stdio.h>
#include "vm.h"

int main(void) {
    // Программа считает (2 + 3) * 4 = 20.
    //
    //   PUSH 2          стек: [2]
    //   PUSH 3          стек: [2, 3]
    //   ADD             стек: [5]          (сняли 2 и 3, положили 5)
    //   PUSH 4          стек: [5, 4]
    //   MUL             стек: [20]         (сняли 5 и 4, положили 20)
    //   HALT            результат = вершина = 20
    int prog[] = {
        OP_PUSH, 2,
        OP_PUSH, 3,
        OP_ADD,
        OP_PUSH, 4,
        OP_MUL,
        OP_HALT
    };
    size_t len = sizeof(prog) / sizeof(prog[0]);

    int result = 0;
    int status = vm_run(prog, len, &result);

    if (status == VM_OK) {
        printf("Программа (2 + 3) * 4 выполнилась. Результат: %d\n", result);
        printf("(ожидаем 20 — если так, твоя машина работает!)\n");
    } else {
        printf("Машина вернула код ошибки %d (см. VmStatus в include/vm.h).\n", status);
        printf("Похоже, vm_run ещё не реализован или в нём баг — это нормально для начала.\n");
    }

    // Демо всегда завершается успешно (0): мы просто показали результат.
    // Не путай с кодом ошибки САМОЙ VM — он выше, в status.
    return 0;
}
