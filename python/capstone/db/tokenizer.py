# его заполняет ученик. Этот файл существует только чтобы доказать, что задачи решаемы и что
# тесты (включая рандомизированные) зелёные на правильном коде. В main он НЕ попадает.
#
# Капстоун, слой 1 из 3 — ТОКЕНИЗАТОР (лексер).
# Задача слоя: превратить плоскую строку SQL в список «атомов» — токенов. Это самый нижний,
# самый тупой слой: он НИЧЕГО не понимает про CREATE/SELECT, не знает таблиц, не считает
# скобки. Его единственная работа — аккуратно нарезать текст. Чем тупее и предсказуемее
# токенизатор, тем проще писать парсер над ним.
#
# Что считается токеном:
#   - слово: подряд идущие буквы/цифры/подчёркивания (users, id, name, age, 30, SELECT, *);
#   - одиночный символ-пунктуатор: ( ) , и операторы сравнения = != < > <= >=;
#   - пробелы/табы/переводы строк — разделители, в токены НЕ попадают.
#
# Тонкости, которые проверяют тесты:
#   - '(', ')', ',' — это ОТДЕЛЬНЫЕ токены, даже если прилипли к слову:
#       "(1,Alice)" -> ["(", "1", ",", "Alice", ")"]
#   - двухсимвольные операторы !=, <=, >= нельзя рвать на два токена:
#       "age>=18" -> ["age", ">=", "18"]
#     а вот "age >= 18" даёт ровно то же самое — пробелы вокруг роли не играют.
#   - регистр и сами слова токенизатор НЕ трогает: "Alice" остаётся "Alice", "SELECT"
#     остаётся "SELECT" (приведение к верхнему регистру — забота парсера, не лексера).
#   - звёздочка '*' — это слово-токен "*".
#
# Подсказка по реализации (один из способов): иди по строке индексом i; на каждом шаге
# смотри текущий символ. Пробел — пропусти. Пунктуатор — добавь как токен (не забудь
# заглянуть на символ вперёд для != <= >=). Иначе — «съешь» весь кусок слова до ближайшего
# пробела/пунктуатора и добавь его одним токеном. Никакой regex не обязателен, но и не
# запрещён (модуль 11 — стандартная библиотека, там есть re).

from __future__ import annotations


def tokenize(sql: str) -> list[str]:
    """Разбить строку SQL на список токенов (строк).

    Токен — это либо «слово» (буквы/цифры/подчёркивания, а также '*'), либо один из
    знаков-пунктуаторов: '(' ')' ',' '=' '!=' '<' '>' '<=' '>='. Пробельные символы
    служат разделителями и в результат не попадают.

    Примеры (это и есть контракт — см. test_tokenizer.py):
      tokenize("SELECT * FROM users")
          == ["SELECT", "*", "FROM", "users"]
      tokenize("INSERT INTO users VALUES (1, Alice, 30)")
          == ["INSERT", "INTO", "users", "VALUES", "(", "1", ",", "Alice", ",", "30", ")"]
      tokenize("age>=18")          == ["age", ">=", "18"]
      tokenize("id != 1")          == ["id", "!=", "1"]
      tokenize("")                 == []

    Возвращает: list[str] — токены в порядке появления. Регистр слов не меняется.
    """
    tokens: list[str] = []
    i = 0
    n = len(sql)
    # Двухсимвольные операторы проверяем ПЕРЕД одиночными — иначе ">=" разорвётся на ">" и "=".
    two_char = ("!=", "<=", ">=")
    single_punct = ("(", ")", ",", "=", "<", ">")

    while i < n:
        ch = sql[i]

        # пробельные символы — разделители, в токены не попадают
        if ch.isspace():
            i += 1
            continue

        # двухсимвольные операторы сравнения
        pair = sql[i:i + 2]
        if pair in two_char:
            tokens.append(pair)
            i += 2
            continue

        # одиночные пунктуаторы: скобки, запятая, одиночные операторы сравнения
        if ch in single_punct:
            tokens.append(ch)
            i += 1
            continue

        # иначе — «слово»: всё подряд до ближайшего пробела или пунктуатора.
        # '*' попадает сюда же и становится словом-токеном "*".
        start = i
        while i < n:
            c = sql[i]
            if c.isspace() or c in single_punct or c in ("!",):
                break
            # начало двухсимвольного оператора (например ">=") тоже обрывает слово
            if sql[i:i + 2] in two_char:
                break
            i += 1
        tokens.append(sql[start:i])

    return tokens
