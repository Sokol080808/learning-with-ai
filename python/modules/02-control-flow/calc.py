# тесты (включая рандомизированные) зелёные на правильном коде. В main он НЕ попадает.


def calc(a: float, op: str, b: float) -> float:
    """Применить арифметическую операцию op к числам a и b.

    Контракт:
      - op == "+"  -> a + b
      - op == "-"  -> a - b
      - op == "*"  -> a * b
      - op == "/"  -> a / b
    Ошибки (оба случая — raise ValueError с понятным сообщением):
      - деление на ноль (op == "/" и b == 0);
      - неизвестная операция (op не из "+", "-", "*", "/").
    Пример: calc(2, "+", 3) == 5.0; calc(10, "/", 4) == 2.5.
    """
    if op == "+":
        return a + b
    if op == "-":
        return a - b
    if op == "*":
        return a * b
    if op == "/":
        if b == 0:
            raise ValueError("Деление на ноль недопустимо")
        return a / b
    raise ValueError(f"Неизвестная операция: {op!r}")
