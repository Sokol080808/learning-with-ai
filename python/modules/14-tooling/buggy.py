from __future__ import annotations


def sum_first_n(xs: list[int], n: int) -> int:
    """Сумма ПЕРВЫХ n элементов списка xs.

    Контракт: вернуть сумму xs[0], xs[1], ..., xs[n-1] — ровно n штук.
    Если n == 0 — сумма пустого набора, то есть 0.
    Считаем, что 0 <= n <= len(xs).
    """
    # БАГ: off-by-one — берём на один элемент меньше, чем просили.
    return sum(xs[: n - 1])


def max_in(xs: list[int]) -> int:
    """Максимальный элемент непустого списка xs.

    Контракт: вернуть наибольшее из значений xs. Список не пустой.
    Работать обязано и для списков, где ВСЕ числа отрицательные.
    """
    # БАГ: неверная инициализация — стартуем с 0, а не с реального элемента.
    # Для списка из одних отрицательных чисел ответ окажется завышенным (0).
    m = 0
    for x in xs:
        if x > m:
            m = x
    return m


def average(xs: list[int]) -> float:
    """Среднее арифметическое непустого списка xs.

    Контракт: вернуть sum(xs) / len(xs) как float. Список не пустой.
    """
    # БАГ: лишняя +1 к сумме — среднее систематически завышено.
    return (sum(xs) + 1) / len(xs)


def has_duplicate(xs: list[int]) -> bool:
    """Есть ли в списке xs хотя бы одно повторяющееся значение.

    Контракт: True, если какое-то значение встречается >= 2 раз; иначе False.
    На пустом списке и на списке из одного элемента — False.
    """
    # БАГ: внутренний цикл стартует с i, поэтому при j == i элемент сравнивается
    # САМ С СОБОЙ — и любой непустой список ошибочно объявляется «с дубликатом».
    for i in range(len(xs)):
        for j in range(i, len(xs)):
            if xs[i] == xs[j]:
                return True
    return False


def repeat(s: str, times: int) -> str:
    """Строка s, повторённая ровно times раз.

    Контракт: repeat("ab", 3) == "ababab". При times == 0 — пустая строка "".
    Считаем, что times >= 0.
    """
    # БАГ: times - 1 — повторяем на один раз меньше, чем нужно.
    return s * (times - 1)
