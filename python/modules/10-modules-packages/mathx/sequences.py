def factorial(n: int) -> int:
    """Факториал n: произведение всех целых от 1 до n.

    По определению 0! == 1.

    Контракт (n >= 0):
      factorial(0) == 1
      factorial(1) == 1
      factorial(5) == 120

    Для отрицательного n факториал не определён —
    подними ValueError (тест это проверяет).
    """
    if n < 0:
        raise ValueError("факториал не определён для отрицательного n")
    result = 1
    for k in range(2, n + 1):
        result *= k
    return result


def fib(n: int) -> int:
    """n-е число Фибоначчи (нумерация с нуля).

    Последовательность: 0, 1, 1, 2, 3, 5, 8, 13, ...

    Контракт (n >= 0):
      fib(0) == 0
      fib(1) == 1
      fib(2) == 1
      fib(7) == 13

    Для отрицательного n подними ValueError.
    """
    if n < 0:
        raise ValueError("число Фибоначчи не определено для отрицательного n")
    a, b = 0, 1
    for _ in range(n):
        a, b = b, a + b
    return a
